                    <p>When I arrived in England in 1959, with overwhelming intensity, I felt that we should pay particular attention to the education and training of the young and the new converts. At that time English literature, covering the subject of tarbiyyat (training) was almost non-existent. So that they may offer their Salaat in the exact prescribed manner, for familiarizing the young and the new converts there was paramount need for a book covering 'Islamic Salaat'. Covering this particular subject, in spite of thorough search, I was not able to find a book or a book in English. There was urgent need for a book, which would explain the fundamentals of the Faith and the correct manner of offering Salaat. Placing my trust in the Almighty I started the preparation of a booklet called 'The Muslim Prayer Book'. With a great deal of effort, this book consisting of 63 pages was published in 1962. It gained immediate acceptance and since then six editions have been published. It has been translated and published in four different languages.</p>
                    <br />

                    <p>The publication Department of the Ghanaian Government printed 20,000 copies and the booklet got integrated into their School curriculum. Another 7,000 copies were printed in Qadian. The book was also published in Nigeria. In an International Shoora, Hadhrat Khalifa tul Masih IV, referred to this book. A Yugoslavian Professor made comprehensive comments.</p>
                    <br />

                    <p>In those days, whenever I had occasions to speak on Islamic subjects in various Clubs and Societies most questions asked related to the status and rights of women in Islam. There was a great deal of misunderstanding and misinformation on this subject. I wrote a book 'The Status of Woman in Islam'. It gained immediate popularity and since then three editions have been published.</p>
                    <br />

                    <p>At the suggestion of Hadhrat Chaudhry Muhammad Zafrulla Khan, I wrote a small booklet in English covering the events that led to the martyrdom of Hadhrat Sahibzada Abdul Lateef.</p>
                    <br />

                    <p>To enable our young to expose and rebut the groundless allegations made by non-Ahmadi clerics against the Jamaat, it was keenly felt that there was a need for a book. I wrote 'Truth about Ahmadiyyat'. Hadhrat Muhammad Zafrulla Khan suggested this name and he very kindly improved its text. The book contained responses to about a dozen different allegations and consisted of about 100 pages. Some very favorable reviews appeared in many places and it became popular within and without the Jamaat.</p>
                    <br />

                    <p>I also wrote a book in English covering the life and character of Hadhrat Khalifa tul Masih III called 'A Glimpse into the Life of Hadhrat Khalifa Tul Masih III.' This book also found ready acceptance.
                        I wrote a book 'Islam My Religion' in English for the upbringing and tarbiyyat (training) of the Ahmadi children. This book also gained popularity. Four editions have been published since and it has been translated into many other languages.</p>
                    <br />

                    <p>During the troublesome and torturous events of 1974 the Ahmadiyya Jamaat in England, through newspapers and other media, embarked on a campaigned to counter the attacks of opponents. We actively responded to the attacks of the opponents and presented facts before the world. Apart from the prestigious newspapers and magazines in Britain, all over the world, many dozens of papers and magazines published our articles. I collected all the relevant clippings and after having arranged them country-by-country published in the form of a book 'From the World Press'. This book, containing 200 pages became very popular. To support their cases Ahmadi immigrants seeking asylum both in England and in Germany, extracts from this book were presented in various courts.</p>
                    <br />

                    <p>In 1985 when I lived in Rabwah, at great insistence of some friends, referring to my association and interaction with Hadhrat Chaudhry Muhammad Zafrulla Khan over the years I wrote a book in Urdu entitled 'Muhammad Zafrulla Khan â€“ Chand Yaadeyn'. The book was still under print when the Secretary of the Majlis Khatem e Nabuwat alleging that I had contravened the provisions of the notorious Anti Ahmadiyya Ordinance of 1984 filed a case against me. While the book was under print an F.I.R., (First Information Report) was registered. The police raided the Printing press, took custody of the book, arrested four non-Ahmadi employees of the Press and put them into jail. While I was touring various Jamaats in the Frontier Province, the police started searching for me. I got to know of their objective and sought guidance from Huzoor who directed me to migrate to Britain. On arrival in London Huzoor appointed me as Additional Wakeel ut Tasneef and Chairman of the 'Review of Religions' magazine. On arrival in England, I published this book, which gained great popularity. Very many Ahmadis told me that in their houses, this book is used for imparting lessons to their children. It is my great desire that this book should be translated into English. May He create conditions that this wish may be realized.</p>
                    <br />

                    <p>In 1986 I wrote a book containing a detailed account of the events that led to the martyrdom of Hadhrat Moulvi Abd ur Rahman and Hadhrat Sahibzada Abd u Lateef. It was named 'Shaheedaan e Rah e Wafa'. The Jamaat in England published it and it gained ready acceptance.
                        When 'Shaheedan e Rah e Wafa' was published very many friends demanded that for the benefit of the young Ahmadis, the recent converts and the public at large it should be translated into English. I asked Brother Anwar Ahmad Kahlon to translate it for me. With great pleasure, he readily accepted this assignment. He said that this undertaking would give him some credit with the Almighty. With a great deal of effort, he translated the book, which was published under the title 'The Afghan Martyrs'. This book also gained recognition and within a few weeks, the first edition was sold out.</p>
                    <br />

                    <p>Sincerely I request all my dear ones and my children who read my autobiography to offer earnest prayers in favor of my brother and my elder Chaudhry Anwar Ahmad Kahlon. May Allah accept his efforts made purely to gain His pleasure. May His doors also remain ajar for his progeny as well. Amen.</p>
                    <br />

                    <p>In 1995, I wrote <strong>'Dars e Ibrat'</strong>, a book in Urdu. In this book, a detailed mention has been made of the disastrous ends of some of the prominent opponents of the Jamaat. A great deal of effort went into the preparation of this 200-page book. Thousands of copies of this book have been sold and it has gained great fame.
                        On two different occasions I have been blessed with opportunities to perform Umra. I have written a 200-page account named, 'Safar Naama Deyar e Habeeb', which awaits publication. The first three chapters of this book have already been serially published in the weekly 'Lahore'. Readers have sent me some flattering reviews. I pray that this book may also be published soon and may lead to the enhancement to the knowledge of the readers.</p>
                    <br />

                    <p>In 1996, I started writing a book covering fourteen different facets of the life and character of the Promised Messiah. Putting in hard work my Brother Anwar Ahmad Kahlon has translated it and in my view, this book will be an important step pertaining to the life and character of the Promised Messiah. The work on this book is still under way and Brother Anwar Ahmad Kahlon has already sent me 100 typed pages translated by him. This book will, I hope, Inshallah, be presented in the form of an attractive educational bouquet. It will serve as a beacon for the English speaking generations. In this book many attractive, faith-boosting events have been covered.</p>


