                        In 1963, a large number of Pakistani Ahmadis arrived in Britain to beat the forthcoming Voucher system introduced by the British Government to curb the influx of immigrants from the Indian sub-continent. Their first port of call was the London Mosque. We made arrangements for their boarding and lodging in the Mission House and in some Ahmadi houses. Our first priority was to find them jobs. Captain Sahib came to our rescue and offered his services to find jobs for many of them in Southall. Numerous job seekers were sent to him and all of them were soon employed in various factories.</p>
