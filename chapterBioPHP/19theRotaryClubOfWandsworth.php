                    <p>In America, at the turn of the century, some business persons, friendly with each other, got together in a restaurant for their midday meal. In their casual conversation, it was mentioned that mostly the business persons remained so engrossed in the promotion of their trade or industry that they had no time left either for social contacts, or for acquiring intimate knowledge regarding subjects other than their own trade or industry. Because of this discussion, it was agreed that, once a week, they would meet for a midday meal and for the most part subjects other than their businesses will be discussed.
                        That is how the Rotary Club was founded. The Rotary Clubs have now assumed an International status. There is hardly a country in the world in which a Rotary Club has not been firmly established. In the developed countries, in the bigger towns and cities, many clubs have come into being. Their members hold regular weekly meetings either at Lunch or at Dinner. Non-members are also invited to the meetings. Speeches are often followed by Question and Answer sessions. The Rotary Clubs collect sizable sums for national charities. The Clubs ensure that that not only do their members create a healthy liaison with the National dignitaries but that they also become acquainted of their views.
                        In England 2,000 clubs are in existence. Membership is confined to those whose financial dealings are above board and those who lead unblemished lives. Further, they should hold a senior position in their business. This way, every Rotary Club, in its own region or town includes dignitaries in its fold.
                        A lot can be written about the Rotary Clubs but the object in writing this article is to talk about the Rotary Club of Wandsworth with which the British Jamaat Ahmadiyya has had a special relationship.
                        The Fazl Mosque, which was the first Mosque, built in London for the worship of the Almighty, is situated in the Borough of Wandsworth. In 1924 the Second Successor to the Promised Messiah, laid its Foundation Stone with his own blessed hands. Sir Abdul Qadir had the great privilege and honor of inaugurating the Mosque in 1926.</p>
                    <br />
                    <p>In 1928 it was decided to establish a Rotary Club in Wandsworth. Hadhrat Moulvi Farzand Ali Khan, the then Imam of the Fazl Mosque, was one of the Founder Members and of course he participated in its very first meeting. He also offered a silent prayer on that occasion. On his return to India his successor, Hadhrat Moulvi Abd ur Raheem Dard was invited to join the Club. Since then this practice has continued. </p>
                    <br />

                    <p>Participation in the activities of the Club not only gave the Imams an excellent opportunity to propagate their faith, but also they had the prospect of meeting local dignitaries and Government officials.
                        Mr Raymond King was a well-known member of the Wandsworth Rotary Club. After joining the Club, I became friendly with him and we intermingled regularly. </p>
                    < br/>
                    <p>Once, I asked him:</p>

                    <div class="quote">"You have had dealings with various Imams, which one of them has impressed you most?"</div>

                    <p>In answer, he said:</p>

                    <div class="quote">"Every Imam had his own individual qualities. Their presence in the meetings always created great interest. However, I was most impressed with Maulana Dard. He had a clear insight into the British disposition. In a way, he dominated the Club. His opinion was always sound and weighty and he had an excellent command of the English language."</div>

                    <p>Mr King quoted an example. He said</p>

                    <div class="quote">"Maulana Dard gave me the manuscript of his book 'The Life of Ahmad' so that I may improve and polish his English. I read the Draft word by word and did not find a single mistake. However, I did offer some advice in regard to his style."</div>

                    <p>Mr King also said that he was also deeply impressed by Maulana Jalal ud Deen Shams who had a profound knowledge of the Christian Faith and he often quoted verses of the Bible from memory.</p>
                    <br />
                    <p>Hadhrat Chaudhry Muhammad Zafrulla Khan addressed the Rotary Club on a number of occasions. He often said that he was an Honorary Member of the Rotary Club of Wandsworth. Many members of the Club told me repeatedly that they were devoted to him.</p>
                    <br />
                    <p>I had been a member of the Wandsworth Rotary Club for nearly 30 years. Normally Rotary Clubs do not invite speakers to talk on religious or political subjects. However, during this period, I had been enabled to convey the message of the true Faith to approximately 500 Rotary Clubs.</p>
                    <br />
                    <p>In 1978, members of the Club honored me by electing me as their President. For a continuous period of 5 years, I had already served as Vice President. On many occasions, members of the Rotary Club met the Second and the Third successors to the Promised Messiah. This way, residents of this area, became familiar with the name and the work of the Jamaat.</p>
