                    <p>Then Bishop Michael Nazir Ali of Rochester, who is of Pakistan descent, presented his paper on 'Tolerance Amongst Religions'. In his speech, the Bishop quoted English version of some verses by the poet Iqbal. In them, there was a prominent mention of the relationship between God and man. Here is the English translation of the verses:</p>

                    <div class="quote">"You made the night, and I the lamp. You the clay and I the cup You – desert, mountain-peak and vale I – flowered, Park and Orchard."</div>


