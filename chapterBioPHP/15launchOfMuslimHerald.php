                    <p>In 1960, while in England, I made up my mind to launch a monthly periodical. The then Imam of the London Mosque, Maulood Ahmad Khan warned me that it was not going to be an easy undertaking. He told me that I must not expect much help from the Mission. After a lot of prayers and in consultation with some friends, I applied for permission from the Centre. At my first attempt, the Centre turned me down. They said:</p>

                    <div class="quote">"Firstly, we do not have the resources to publish a periodical. Secondly, often, in their zeal, some missionaries launch periodicals and then are unable to continue. Consequently we suffer disrepute."</div>

                    <p>I wrote to the Vakilutabsheer that if I was given the permission to start publication of the magazine I would be prepared to be fully responsible for meeting all the relevant expenses. I also pledged that I would not seek any financial assistance, either from the Centre or from the British Mission. Finally, subject to the following two conditions the Centre granted me permission.</p>
                    <br />
                    <p>I would wholly support the periodical financially and no help would be sought from the Centre or from the British Mission.
                        2. The address shown on the periodical would not be that of the Mission House in London and it would not be attributed to the British Mission.
                        I happily accepted both conditions. In the early years, instead of 63 Melrose Road, the address shown was 61 Melrose Road.
                        The first issue of the periodical appeared in January 1960 and in it were published messages from Hadhrat Chaudhry Zafrulla Khan, Sahibzada Mirza Mubarak Ahmad and Basharat Ahmad Bashir. An article entitled 'Some Misconceptions about Islam in the West' by Dr. Muhammad Naseem appeared in the first issue. Only five hundred copies were printed and distributed free of charge. With great effort and running around "Pakistan International Airlines" gave us an advertisement. In the beginning that constituted our total income. The remaining expense was borne by Abdul Azeez Deen, Moulvi Abd ur Rahman and myself.</p>
                    <br />
                    <p>By the Grace of Allah, the very first issue was well received and Hadhrat Chaudhry Zafrulla Khan and Dr. Abd us Salaam encouraged me significantly.
                        For the second edition, after a lot of legwork, we were able to secure some more advertisements. Apart from its 'Title page' the February issue consisted of 32 pages. The magazine was printed on Art paper and included articles on the following subjects:</p>
                    <br />

                    1. 'The Need for Effective Balance between Religion, Philosophy and Science' By Field Marshall Muhammad Ayub Khan President of Pakistan.
                    2. 'The Powers and Duties of the Islamic State' By Hadhrat Khalifa tul Masih II.
                    3. 'Technology and Pakistan's Attack on Poverty' By Professor Abd us Salaam FRS. SPK.
                    4. 'Islam or Communism' By Hadhrat Mirza Bashir Ahmad M.A.
                    5. 'The Holy Month of Ramadan By Maulana A. R. Dard M.A.
                    <p>This issue was sold out very rapidly and in view of the huge demand, we had to print extra copies. Some amongst the non-Ahmadis were also pleased with its contents.</p>
                    <br />
                    <p>I was its Founder, its Editor and its Financier.</p>
                    <p>In order to obtain advertisements from owners of Pakistani restaurants I had to wait until the restaurants were closed, sometimes as late as 2 a.m. In this task Moulvi Abd ur Rahman helped me a great deal. Being the owner of a canteen, he would accompany me and assist me in obtaining advertisements from other catering establishments. May Allah reward him abundantly. Amen.
                        By the Grace of Allah, the Periodical became financially self-sufficient. Articles of a very high order began to be published. Because of its popularity, the number of subscribers increased. It began to be distributed all over the world. In this manner, a seed that was planted assumed the shape of a healthy tree.</p>
                    <br />
                    <p>In August 1962 Mr A.R. Chaudhry, who had migrated from East Africa, became a Joint Editor with me. With his assistance, the standard of the articles published in the periodical was further improved. During this period, some special issues were brought out such as: 'European Missions', 'Christianity', and 'Hadhrat Abdul Lateef'.</p>
                    <br />
                    <p>In the April 1961 issue an article entitled
                        'What the War in Algeria is Costing France'
                        Written by Mr R. Karachi, a non-Ahmadi journalist of repute was published. This article gained huge popularity amongst the supporters of the Movement for Algerian Freedom from France. I was asked to print an additional 500 copies of that issue for which immediate payment was made. The readers also said that they would persuade some of the Algerian community in England to subscribe to the 'Muslim Herald'. Therefore, we printed another 500 copies and due to their support, we gained some more subscribers. This opened a door for Tableegh amongst the Algerian community.
                        Soon thereafter, I received a letter from the Press Section of the American Embassy in London. I was asked to visit them. When I called at the Embassy, I was extremely surprised to see a number of our periodicals at the desk of the officer concerned. The officer expressed satisfaction concerning the periodical. I was asked how many copies were being printed. I told him that although only 1,000 copies were printed but the readership was much more. The American said that he would like to see at least 5,000 copies printed. Then he enquired about my remuneration as the Editor. I told him that I was a Missionary of the Ahmadiyya Jamaat and merely to earn the pleasure of Allah I had undertaken this work without any remuneration. He said:</p>


                    <div class="quote">"What use is voluntary work? You should be paid for it."</div>

                    <p>These remarks greatly irritated me. I said:</p>


                    <div class="quote">"Why don't you tell me plainly what you want?"</div>

                    <p>In reply, he said:</p>

                    <div class="quote">"I propose that you should print 5,000 copies of the periodical. We shall pay you an adequate salary and you may continue to work as a Missionary. In return, every now and then, we would like you to publish material against Communism. We would expect you to publish any articles and news given by us to you without any alteration."</div>

                    <p>On hearing these remarks, I got up and said:</p>

                    <div class="quote">"I am a Missionary of the Ahmadiyya Jamaat and I only publish the periodical for the propagation of Ahmadiyyat and Islam. As a Jamaat, on principle, we also oppose Communism vehemently. Already some articles on this subject have appeared in the periodical. However, I cannot possibly be a servant of the American Government, nor can I continue to edit this periodical in subordination to the American Embassy." There the meeting ended.</div>

                    <p>His Mercy and Grace blessed this periodical with opportunities to serve Islam and Ahmadiyyat. Through it, Imam Zook of Poland embraced Ahmadiyyat. This is another story, which is recorded elsewhere. During the torturous days in 1974, this periodical played a crucial role. Attacks from the opponents were strongly rebutted. News of the persecution faced by the Jamaat in Pakistan with relevant photographs was published. Later on, largely based on the material that had appeared in this periodical, a whole book called 'From the World Press' was published. This book proved to be of great help in supporting the cases of asylum seekers in Germany.</p>
                    <br />
                    <p>A little later Syed Mansoor Ahmad Shah also became an editor. His articles gained great popularity. With his help and co-operation, the periodical made further strides. May Allah reward him abundantly. I am deeply indebted to him as he translated into English many articles that I had written in Urdu. He also revised my books. In every way possible, he co-operated with me in an admirable manner. The periodical continued to be published until 1984. For the first four or five years, I was responsible for meeting all the necessary expenses. Then the British Mission assumed the financial responsibility and freed me from this burden.
                        In 1975, the University Microfilms Ltd. of England saved all the earlier issues on a microfilm.</p>


