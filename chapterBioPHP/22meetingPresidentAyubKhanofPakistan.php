                    <p>In 1962 when the former Chief Martial Law Administrator, Field Marshal Muhammad Ayub Khan, the President of Pakistan came to England he stayed in Claridges Hotel. Apart from having sent him a telegram welcoming him on behalf of the Ahmadiyya Jamaat in Britain, a request was made for an audience, to which he kindly agreed and indicated a time for a meeting. According to the program, a delegation consisting of the members of the Jamaat, in which I was also included, went to the Hotel. On arrival, we discovered that the President had cancelled all his appointments for the day as he had received news that an American U 2 Spy plane, as it was flying over their space and had taken off from Pakistan, had been shot down by the Russians.</p>
                    <br />

                    <p>Mr Khrushchev had threatened that he had put a red ring around Peshawar. If need be, he said, he would not hesitate to destroy Peshawar. This news prominently appeared in all the English newspapers. When we arrived at the Hotel the Military Secretary to the President told us that although the President had cancelled all other appointments for the day, the President had however instructed that he would meet the Jamaat Ahmadiyya delegation. We sat down in the waiting room. Outside the President's room were TV cameras, press representatives and some government officials. When we saw this array, we felt certain that in these circumstances a meeting would not be possible and that the President would be justified in canceling the meeting.
                        After a short while, the Military Secretary told us that the President would see us. However, he suggested that the meeting should not last more than ten minutes as the press representatives wanted to ask the President many questions. We entered the President's room and he met us with great humility. He told us that before his departure from Pakistan he had received a request from the Ahmadiyya Jamaat for provision of Foreign Exchange for the construction of Mosques in Foreign Countries. He said that on receipt of this request he sent for the Finance Minister and asked him to accept the application for Foreign Exchange. He added that the Finance Minister said to him:</p>

                    <div class="quote">"Sir, if this application from the Ahmadis is granted then the Mullahs would create a rumpus and that it is quite possible that they too would apply for Foreign Exchange and currently there is a dearth of Foreign Exchange."</div>

                    <p>He said he told the Finance Minister</p>

                    <div class="quote">"Jamaat Ahmadiyya was the only Jamaat that was propagating Islam throughout the world. This work is not financed by any government but is wholly based on financial contributions by the poor members of the Jamaat. Have the Moulvis ever served Islam abroad? They only agitate and disturb law and order. I therefore direct that the application from the Jamaat may be granted and if the Moulvis create disorder I will settle with them myself."</div>

                    <p>The President said to us:</p>

                    <div class="quote">"You are rendering great service in Africa and I value this endeavor. You may convey a message to your Centre that any Foreign Exchange needed for propagation of Islam in Africa will be provided."</div>

                    <p>After a lapse of ten minutes, the Military Secretary opened the door and signaled to us to conclude the meeting. Looking at him the President asked the Military Secretary to send in some tea and coffee for us. He asked us if we were in a hurry. In answer, we told him that we were in no hurry but due to his pre occupations, we did not wish to take up too much of his time. The President said that only the Press Representatives were waiting outside. He said that they could wait and we could stay. He enquired how many, because of our propagation, enter Islam in England in one year. We said that although only very few embrace Islam but the misunderstandings regarding the teachings of Islam existing in the minds of the British public are removed. We told him that the orient-lists and clergy present a horrible picture of Islam and we challenge them and deal with them energetically and in this field, we were gaining success. This way we said, the misunderstandings that arise in the minds of the public were being removed. We said that we felt that after a while the day would dawn when people would begin to embrace Islam in droves.
                        The President said that it was his wish that we should further boost our efforts in Africa so that Islam would spread there rapidly. He further said that if the majority of the African countries became citadels of Islam, God willing, in the International Forums, Islamic views would begin to be heard with greater attention. He said that he had made a deep study of African mind and had concluded that the African were not biased or prejudiced. However, they were somewhat na√Øve. Once they understand a proposition, he said, they stick to the truth firmly and do not care for opposition to the truth. We advised the President of the service that the Jamaat was rendering in Africa. The meeting, which was meant to last for ten minutes, continued for forty-five minutes. We left happily and were deeply impressed by his manners, simplicity and forthright talk.</p>
                    <br />

                    <p>A few years later the President paid another visit to the United Kingdom. The Pakistan High Commissioner in Britain called me and told me that the President was shortly to visit the United Kingdom. He said that the President himself had directed that amongst those who would be present at the Airport to welcome him the Imam of the Fazl Mosque in London should be included. The High Commissioner very kindly sent an Embassy car for me. A whole row of people stood next to the aircraft from which the President alighted. He shook hands with all of them and when it was my turn, with a smile he asked:</p>

                    <div class="quote">"In this year how many Christians have you converted to Islam?"</div>

                    <p>Then, addressing the High Commissioner he said:</p>

                    <div class="quote">"The Imam should be invited to all the functions that are to be held during my visit to England."</div>

                    <p>I was deeply impressed by his magnanimity and enviable manners. In 1971, I had the great privilege of serving Hadhrat Khalifa tul Masih III as his Private Secretary. In August 1971, Huzoor went to Islamabad and stayed in a rented house. In that year a Moulvi had made an attempt to kill Mr M.M. Ahmad. He had suffered grievous injuries but miraculously the Almighty saved his life. Because of this incident, Huzoor's stay in Islamabad continued to be extended. We learnt that the former President of Pakistan, Field Marshal Ayub Khan, was spending his days of retirement in a nearby house.</p>

                    <p>Sahibzada Mirza Fareed Ahmad and I sought permission from Huzoor to visit the Field Marshal. With Huzoor's permission, we obtained an appointment with him.</p>
                    <br />
                    <p>At eleven o'clock the next day, we went to see the Field Marshal. I was extremely surprised at the sharpness of his memory. As soon as we entered the drawing room, he said:</p>

                    <div class="quote">"Do you not now live in London."</div>

                    <p>I submitted that Huzoor had recalled me to Pakistan and that currently I was serving as his Private Secretary. We talked on various subjects and he took particular interest in the progress of the Jamaat. Then, most unassumingly, he asked us to request Huzoor to pray for him. He also said that he would like to present himself before Huzoor. When we reported the details of our visit to Huzoor, we also told him that the Field Marshal was in poor health and looked frail. At this Huzoor said:</p>

                    <div class="quote">"In that case I will go and see him."</div>

                    <p>That was settled. An appointment was made and Huzoor called on him at his residence. The Field Marshall came out in the porch to receive Huzoor and escorted him to his sitting room. Huzoor spent quite a while with him. The Field Marshal himself came as far as the car to see him off. Both on Huzoor's arrival and on his departure he himself opened the door of Huzoor's car. As Huzoor was leaving, once again he made a request for prayers.</p>


