                    <p>In 1959 when I reached London Maulana Muhammad Yaqoob Khan was the Imam of the Woking Mosque. He belonged to Pirpiaiee, a village at a short distance from my village. He was on extremely friendly terms with my father. In the early days, he was the Headmaster of the Muslim Model School in Lahore. Hadhrat Mirza Muzaffar Ahmad was one of his pupils. For many years, the Maulana edited the Daily Civil and Military Gazette. Later on, he also edited 'The Light', a magazine belonging to the Anjuman Ishaat Islam. During the Kashmiri's struggle for liberation, under the guidance of Hadhrat Khalifa tul Masih II the Maulana also had the good fortune of making a significant contribution.
                        At the time of my departure from Pakistan my father had asked me to meet the Maulana as he was a perfect gentleman and also in accordance with the Pukhtoons traditions he was very hospitable.</p>
                    <br />
                    <p>A few months after my arrival in London I received a letter from my uncle (Khaloo) Hadhrat Qazi Muhammad Yusuf, Amir of the Frontier Province. He had instructed me to convey his letter to Maulana Muhammad Yaqoob. He had also instructed me that I should read the letter before delivering it. The gist of the letter written by Qazi Sahib to the Maulana was as follows:
                        When Maulana Yaqoob Khan was, a student of the Islamia College in Peshawar Hadhrat Qazi Sahib regularly visited the college to convey the message of Ahmadiyyat. The Maulana was one amongst those under his Tableegh. Qazi Sahib had given the Maulana a few books by the Promised Messiah (pbuh) to read. Qazi sahib said that every week he would go to the college and answer any questions raised by the Maulana. This process continued for quite a while. Finally the Maulana swore allegiance at the hand of Khalifa tul Masih I. Qazi Sahib added that when he was under Tableegh, Qazi Sahib never introduced the Promised Messiah to him as a mere Reformer. Instead, he always presented him as a Subordinate Prophet. When the Maulana swore allegiance, he surely believed that the Promised Messiah (pbuh) had been granted the station of a Prophet.</p>
                    <br />

                    <p>Qazi Sahib continued and said that he was swearing by God that when he placed his hand at the hand of the Promised messiah (pbuh) for Baiat' he was absolutely certain that he was placing his hand at the hand of the 'Prophet of the Age'. Qazi Sahib then posed a question.</p>
                    <br />

                    <p>'After having accepted the Promised Messiah (pbuh) as a prophet, influenced by Moulvi Muhammad Ali Sahib why you have strayed away from the true path?'
                        As soon as I received the letter, I telephoned the Maulana and introduced myself. I also told him of the letter that I had received for him from Hadhrat Qazi Sahib. I also conveyed to him my father's greetings. The Maulana appeared very pleased at having received my telephone call. He said that Qazi Sahib's letter could have been sent to him by post but it was his wish that I should visit him and meet him. Accordingly he invited me for lunch on the following Sunday when I was received with great warmth. As soon as I looked at the Maulana's countenance, I was convinced that this face belonged to a Muttaqi. He was handsome, tall, with a white beard and had a large Karakul hat on his head.</p>
                    <br />

                    <p>To begin with, we talked about Hadhrat Qazi Sahib and my father. Maulana Muhammad Tufail, at that time Imam of the Berlin Mosque and who, later on, for a long time, served as Imam of the Woking Mosque, joined us for a sumptuous meal. The conversation that ensued was both interesting and enlightening. Maulana Tufail said:</p>

                    <div class="quote">"For no reason at all you believe that Mirza Mahmud Ahmad is the Musleh Maood. The Musleh Maood will appear a lot later. Have you any proof that Mirza Mahmud Ahmad is the Musleh Maood?"</div>

                    <p>I said:</p>

                    <div class="quote">"Surely you believe the Hadeeth in which the Holy Prophet (pbuh)said that Satan cannot assume his appearance and surely you will also agree that the Satan cannot assume the resemblance of the Promised Messiah (pbuh)"</div>

                    <p>Maulana Tufail said that he accepted this tradition to be true. Then I said:</p>

                    <div class="quote">"I swear by the Almighty that I have seen a very clear and transparent dream in which the Promised Messiah confirmed that Mirza Mahmud Ahmad was the Musleh Maood."</div>

                    <p>I related my dream as follows:</p>

                    <div class="quote">"In a dream I saw a raised platform in the middle of a vast ground in which there were a large number of people assembled. A few were seated on the raised platform and I was one of them. Right in the middle of the raised platform sat Hadhrat Mirza Mahmud Ahmad on a chair. There was complete silence in which one could hear a pin drop. It was announced that the Promised Messiah (pbuh) is on his way. I saw the Promised Messiah taking slow steps towards the platform. He had a staff in his hand and was wearing a long robe. In a short while, he got to the chair on which Hadhrat Mirza Mahmud was seated. Huzur placed his hand on the shoulder of Hadhrat Mirza Mahmud Ahmad and said;
                        <br />
                        'Some people are spreading misinformation concerning this son of mine. Today I wish to make it quite clear as to what his status is in the spiritual world.' Then in a loud voice, the Promised Messiah repeated the words of the prophecy concerning the Musleh Mauood. After mentioning each attribute, pointing towards his son, he said that this attribute had been granted to him. In this manner he read the whole of the prophecy and again and again confirmed that it pertained to Hadhrat Mirza Mahmud Ahmad. In the end, in a most inspiring manner the Promised Messiah said; 'Remember that Mahmud is a solid rock, whoever collides with him will be out of order into smithereens and with whosoever he collides with will be broken into smithereens'. After this the Promised messiah departed."</div>

                    <p>I asked Maulana Tufail what he had to say in this regard. He said that since I had seen the dream it might be a proof to me.
                        Maulana Muhammad Yaqoob listened to our conversation in complete silence. After lunch I offered two Nawafil in the Shah Jehan Mosque and then took leave of the Maulana. I asked him if he was going to reply to the letter from Qazi Sahib. The Maulana said that he would send a reply to Qazi Sahib's letter by post. A few days later, I received an open letter from Maulana Yaqoob Khan, addressed to Qazi sahib written in Pushto, which I read. In his letter, he said that every word contained in Qazi Sahib's letter was true. He tried to justify and explain his position by saying that the boat in which he was sailing had covered a great distance. However, he did ask for prayers. I posted the letter to Qazi Sahib.</p>
                    <br />
                    <p>Later on I met the Maulana on many occasions and during every meeting, I found him absolutely full of praise for the Musleh Maood. I was indeed surprised that although he belonged to the 'Ehl e Paigham', in his own heart he had such great esteem for the Musleh Maood.</p>
                    <br />
                    <p>A short while later, before his return to Pakistan, the Maulana came to the London Fazl Mosque to see me and my wife Salima Begum. He said that he had made up his mind to spend a whole day with us. I thoroughly enjoyed his interesting and instructive discourse.</p>
                    <br />
                    <p>Shortly after his return to Pakistan he wrote to Hadhrat Khalifa tul Masih III saying that he wanted to swear allegiance at his hand. To enable him to fill in the necessary form for joining the fold Huzoor directed Hadhrat Maulana Abul Ata to meet Maulana Muhammad Yaqoob. After signing on the dotted line the Maulana asked Maulana Abul Ata to particularly advise me in London as I also had contributed in his joining the fold. Maulana Abul Ata wrote to me and congratulated me.</p>
                    <br />
                    <p>In the same year during the Jalsa Salaana at Rabwah Hadhrat Maulana Muhammad Yaqoob summoned me and met me with great warmth. He offered very many prayers for me and then he made enquiries concerning the Mosque in Woking. I told him that the Mosque was no longer in the custody of 'Ehl e Paigham' but was in the control of non Ahmadi Muslims. He said:</p>

                    <div class="quote">"That is good; in that Mosque a Baiat in the name of the Promised Messiah could not be managed, how could such a Mosque then be linked with Ahmadiyyat?"</div>


