                    <p>The meeting continued until midday and after that, participants were given an opportunity to intermingle with each other. Apart from discussing various matters with other participants, I had a lengthy conversation with Bishop Michael Nazir Ali. For the atrocities being perpetrated on their members in Pakistan, he sympathized with the Ahmadiyya Jamaat. He stressed the need for followers of all major religions to use their best endeavors to create an atmosphere of tolerance so that no one should be persecuted merely because of his dissimilar beliefs.
                        At 1 o'clock, at the dining table, all participants sat around the Prince. Arrangements had been made for a group photograph with the Prince. Since I also appeared in the photograph, I was sent a copy. I was extremely surprised to see that amongst the participants, I was the only one wearing a hat – all others were bare headed. After the photograph had been taken the Prince once again shook hands with everyone and left in a motorcar. I regarded this occasion as a blessed one as I was enabled to convey the message of Islam to them.</p>


