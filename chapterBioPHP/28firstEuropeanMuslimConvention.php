                    <p>In 1965 in the course of Hadhrat Chaudhry Muhammad Zafrulla Khan's visit to London, in an inspiring manner, he made a mention of the names and their sincerity to the Faith of several European converts. He expressed a desire to hold a convention of such European Ahmadis so that they may be provided an opportunity to interact with each other. Ways and means, he said, of their further education and training could be discussed in this convention. I offered my services for the holding of such a convention and said that the Centre (Rabwah) permitting the convention could be held in London. I accepted responsibility for making all the necessary arrangements. I wrote to Rabwah and they granted permission. By the Grace of Allah a successful convention was held. A report, by Mr A.R. Chaudhry covering this event, was published in the 'Muslim Herald' issue of August/September 1965, which follows.</p>
                    <br />
                    <p>"<strong>The First European Muslims Convention</strong>"</p>
                    <br />
                    <p>With the ever-increasing activities of our Missions on the continent of Europe, it was thought appropriate to have a get together of the Ahmadi Muslims in order to foster the bond of spiritual fraternity and faith amongst them. Accordingly, the Ahmadiyya Jamaat of Great Britain as the host made all possible arrangements for the delegates who came to London to take part in the deliberations of the convention. The London Ahmadis generously offered the guests free accommodation, transport and their hospitality. The Lajna Amaulla, (the organisation of Ahmadi ladies), undertook to cook meals for them during their stay here. They did this painful job in an admirable cheerful manner.</p>
                    <br />
                    <p>Mr A.S. Madsen (Denmark) in his speech on the propagation of Islam in Europe put forwards some of the new ways and means to achieve our object. He suggested the opening of the study circles at each centre and laid stress on giving training to the new converts to the faith. Sir Muhammad Zafrulla Khan endorsing his views added that the time had approached when the local individuals could be usefully trained to assist the Pakistani missionaries in their work.</p>
                    <br />
                    <p>In the afternoon, Sir Muhammad Zafrulla Khan addressed the audience on the important subject of existence of God and on establishing a firm connection with Him. Quoting several verses from the Holy Quran he explained to them that the true and lasting peace of mind and tranquillity could only be had through ones own relationship with Him.
                        On Monday, Mr A.R. (Switzerland) gave his commendable talk on 'Some Aspects of Islamic State', while Mr Eriksson (Sweden) gave his views on the crucifixion of Jesus. Mr Muhammad Naim-ul-Islam (Holland) spoke quite convincingly on Islam and Christianity. All the three talks had an exclusive standard of their own, which provoked a good discussion.</p>
                    <br />
                    <p>Mr E.A. Kunzi read out his admirable paper on the quest of the real God. His speech 'From Humanism to the Unknown Real God' is being reproduced in this issue.</p>
                    <br />
                    <p>The women delegates also addressed the gathering in a series of sweet little talks. Miss J. Koopmann, Miss N. Esseiva and Miss F. Paganini had the charming simplicity of their address, which was undoubtedly rich with ideas.
                        Mr Ibrahim Odeh conveyed the message of affection from the Ahmadis of Kababeer (Israel). Dr. M. Nassem read his scholarly paper on <strong>'The Missionary Spirit in Islam'</strong>.</p>
                    <br />
                    <p>At the close of the four days session Miss M. Cummins, on behalf of the U.K. Jamaat, thanked the guests for their inspiring talks and for the interest, they had taken in this religious gathering.
                        At the end, a meeting of the elected representatives and the missionaries took place. Sir Muhammad Zafrulla Khan was in the chair. The discussions centered round the following points:
                        Difficulties in the way of propaganda of Islam in Europe.
                    <ul>
                        <li> Cultural conflicts</li>
                        <li>Need of an Islamic journal for Europe</li>
                        <li>Need of strengthening consciousness of Islamic brotherhood and personal relation between individuals and mission</li>

                    </ul>


                    <p>Some useful points emerged out of the discussions. Here was a small group of enthusiasts who took stock of their achievements, discussed their problems, reviewed their needs and planned to speed up their work. They seemed to join hands for the massive conquest of Europe for the Muslim faith. It should be noted here that besides the few intelligent speeches which added freshness to the proceedings of the convention, the contributions of Sir Muhammad Zafrulla Khan by way of advice and guidance was undoubtedly great. His presence brought confidence to the members and his benevolent effect on the convention was to tug them all towards conformity by establishing the true spirit of common objective and ideal Islamic zeal.</p>
                    <br />

                    <p>    It is to be added here that Lajna Imaulla gave a special reception to the lady guests during the convention. Mrs Tahira Chowdry welcomed them in an address. Mrs Nusrat Esseiva (Switzerland) in giving the reply thanked them for their hospitality.</p>
                    <br />
                    <p>  The success of the convention rested on the untiring zeal of Mr B.A. Rafiq, the Imam of the London mosque and his executive committee. The Lajna, too, deserves credit for the hard work they had put in for a number of days. May Allah bless them all with His favor. Amen.</p>


