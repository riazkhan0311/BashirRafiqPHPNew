                        <h2>Arrival at London.
                            <br>
                            <small>1960</small>
                        </h2>
                        <p>At Euston Station we were received by Imam of the London Mosque Maulood Ahmad Khan, Abdul Aziz Deen, Chaudhry Muhammad Ashraf, Maulvi Abdur Rahman and Professor Sultan Mahmood Shahid. We proceeded towards the Mission House in Maulvi Abdur Rahman Sahib's car.The whole of London was brightly lit with Christmas decorations.  The streets and the shops were all lit up with decorative Christmas lights. Up until then I had never seen such a big city.</p>
                        <br>
                        <p>On the way Maulvi Abdur Rahman gave us a running commentary on the various historical buildings that we passed en route. Finally, we arrived at 63, Melrose Road (the Mission House) where the Imam had arranged dinner for us. For the first time in three weeks, we ate spinach with meat, which we greatly relished. During the sea voyage, for fear that it may not be Halal, we had refrained from eating any meat.</p>
                        <br>
                        <img class="img-responsive img-border img-full" src="../img/biography/myEarlyLifeInEngland/untitled16.jpg" alt="">
                        <br>
                        <p>After dinner, the Imam took us to 61 Melrose Road and showed us into a two-bed room flat on the fourth floor. That was to be our future residence for the next four years. The house at 61 Melrose Road was large and spacious and apart from a basement, there was the ground floor and two other upper floors. Ahmadi tenants occupied two stories of the house. </p>
                        <br>
                        <p>It was very cold that first night. It was rare for houses to have central heating in those days. Normally coal fires or kerosene heaters were used for heating the houses but in our flat, we did not have either of these two facilities. We wrapped ourselves in the quilts that we had brought from Pakistan but despite that, we hardly slept due to the severe cold.  </p>
                        <br>
                        <p>In 50's and 60's London was well known for its very severe winters. </p>
                        <br>
                        <p>In the morning, we went across to the Imam’s residence for breakfast. He was solicitous and asked if we had a comfortable night. I felt obliged to tell him that never in all my life had I felt so cold during the night; even in Rabwah we used electric heaters. The Imam said:</p>
                        <br>
                        <div class="quote">“There is no provision for these items in our budget.”</div>
                        <br>

                        <p>In the afternoon Maulvi Abdur Rahman came to the Mission House. He heard our account of the bitter cold over the previous night. Since he was also a member of the Finance Committee he confirmed that there was no room in the budget for any extras, but he graciously promised that he would take it upon himself to arrange a kerosene oil heater.  In a short while, he returned with an Aladdin kerosene oil heater and taught me how to light it. Soon the room became cozy and warm and we breathed a sigh of relief. May Allah grant him an exalted station in Heaven. I cannot ever forget this favour that he did me.</p>
                        <br>
                        <p>After a few days the Imam gave me a list of the members of the UK Jamaat. He said that there were more than two hundred names on the list, (of which the majority were students), some of who may have in fact returned to their respective countries. He asked me to contact all members and note down their correct addresses so that we could establish contact with them, and this may also help with the collection of their financial contributions.</p>
                        <br>
                        <p>After several days’ hard work I was able to identify a hundred and forty nine members. That was the total membership of the UK Jamaat in 1959. The vast majority of the members were young and only a very few lived with their families. </p>
                        <br>
                        <p>Apart from Maulvi Abdur Rahman Sahib, Chaudhry Muhammad Ashraf Sahib and Maulvi Abdul Kareem Sahib, no other member in the entire Jamaat possessed a car. All of these three cars were in regular use by the Jamaat. Whenever the Jamaat called upon these three individuals, they would come to the Mission House with their cars. They even paid for the petrol out of their own pocket. </p>
                        <br>
                        <p>I believe Maulvi Abdur Rahman was sent to England as a Commerce Missionary. Later on, having obtained leave from Waqf, he settled in England and was a devoted servant of the Jamaat. He was an active businessman and had set up different businesses over the years.</p>
                        <br>
                        <p>In 1958, he held a contract to run a canteen attached to the Students’ Hostel owned by the Pakistani Embassy. Through him, many members of the jamaat were able to approach and seek assistance from the Pakistan Embassy. I cannot recall a single occasion where Maulvi Sahib declined to assist. He would rush to the Mission House whenever he was called for any errand.</p>
                        <br>
                        <p>Around this time Chaudhry Muhammad Ashraf Sahib was also living in London. He was a sincere, devoted servant of the Faith and in addition he was very hospitable by nature. On many occasions, he would be adamant and invite friends over to his house and entertain them with good food. His wife, Mrs. Ashraf, later on became President of Lajna Imaullah UK. Like her husband she was incredibly hospitable. She entertained those who visited her home but she also brought all kinds of delicacies to the meetings at the Mission House. Many Ahmadis used Ashraf Sahib’s car for their own personal purposes and it was often jokingly referred to as the Mission House's ‘free taxi’ service. He always refused to accept any contribution towards the cost of petrol. I travelled with him many times to places hundreds of miles from London; he would drive me happily and with a great measure of pride and yet in spite of my repeated requests would not even let me pay for the cost of the petrol. May Allah be pleased with him and grant him an elevated station in Heaven. </p>
                        <br>
                        <p>Hazrat Mir Abdussalam Sahib was a Companion of the Promised Messiah. Before his arrival in Britain he was the Amir of Sialkot Jamaat. He was a Cricketer and a Sportsman.</p>
                        <br>

                        <p>    Hazrat Mir Sahib was a handsome person and was always well dressed. He had full command over English language and was a great Orator and Debater. His knowledge of Bible and the Holy Quran was vast. On my arrival he warmly welcomed me and offered to assist me in my missionary work. I was greatly impressed by his vast knowledge, piety and righteousness.</p>
                        <br>
                        <p>   Hazrat Mir Sahib was a regular speaker on Islam at Hyde Park Corner, on Sundays. He invited me to attend his oratory sessions and help him in carrying his make shift rostrum. This was a most informative and educative experience for me and I enjoyed these sessions. After his speech there used to be a lively Question Answer session. Hazrat Mir Sahib encouraged me to take part in it and to answer some questions. I was not fluent in English then and was reluctant to answer questions but Hazrat Mir Sahib insisted that I should take part in it. Slowly I gained confidence and was able to take part in the discussion.</p>
                        <br>
                           <p> After his oratory sessions we would go to a café in Marble Arch and would also invite some Non Muslims for further discussions on various Islamic aspects. Hazrat Mir Sahib believed in the freedom of speech and expression and would patiently listen to those who raised objections against Islam. He never lost his temper and was most considerate.</p>
