                        Ata ul Mujeeb Rashed M.A. started assisting me with effect from 2nd September 1970 and continued until 25th September 1973. He is a son of Hadhrat Moulvi Abul Ata who was my teacher and also my benefactor. With great credit and in a most admirable manner he discharged his duties in the field of Tableegh. His support in certain other responsibilities of the Mission was exemplary. In his own right he is a Scholar of the Faith. He was exceptionally courteous and humble. He treated me with great love and extended his co-operation unstintingly. May Allah reward his efforts in the very best manner. Currently he is both Missionary in Charge in Britain and Imam of the London Mosque.</p>
