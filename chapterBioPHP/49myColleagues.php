                    <p>Major Abdul Hameed retired from the Army and devoted his life to the service of the Community. On 22nd March 1962, he was sent to Britain assist the Mission. In those days, Chaudhry Rehmat Khan was the Imam and I was the Deputy Imam. The Major was appointed Secretary of the UK Mission. In the matter of Tableegh, he was truly passionate. Based on his extensive experience in the Army he reorganised the Mission Office on proper lines. On 13th April 1964, he was sent to Washington as a Missionary for America.</p>
                    <br />

                    <p>Abd ul Wahab Adam arrived in England on 16th October 1972 and worked with me as Deputy Imam. Particularly in 1974 when in Pakistan the Jamaat passed through a turbulent phase, to convey the Pakistan news to the Missions all over the world he worked along with me day and night. He was a tireless worker and in the matter of obedience and zeal he became an example worth following. He played an important role in enlivening the Press. He was equally proficient and fluent in English, Arabic and Urdu languages and paid particular attention to the use of appropriate idioms of the three languages. He rendered valuable assistance to me in the matter of the 'Muslim Herald'. May Allah reward him abundantly. Having been appointed the National Ameer of Ghana he departed from England on 5th December 1974.</p>
                    <br />

                    <p>Bashir Ahmad Orchard having served in British Guyana after being demobbed he was sent to the UK. He assumed charge of the Glasgow Mission, which he had also held in earlier years. He was the first British Missionary of Islam. Due to his co-operation and obedience, his work with me was commendable. He wrote articles for the 'Muslim Herald' and he started a two page pamphlet styled as 'Scotland Gazette'. He was most God fearing, a true Muslim and a Wali ul Allah. He had a very simple lifestyle. May he be given a special place in Paradise.
                        Munir ud Deen Shams arrived in England on 2nd July 1973 and worked with me for a long period as Deputy Imam. He was enabled to render valuable service in the field of education, Tarbiyyat and administration. Currently he is serving in London as Additional Wakeel ut Tasneef. He is a son of Hadhrat Maulana Jalal ud Deen Shams, a former Imam of the London Mosque.</p>
