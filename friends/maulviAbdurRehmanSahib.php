                    <p>I arrived on 19th February, 1959 and took charge as Deputy Imam of The London Mosque. The next day a gentleman called Maulvi Abdur Rehman introduced himself to me. He told me that he was sent in early forties by Hazrat Khalifatul Masih II as a trade missionary to Britain. He worked in that position for sometime and then he got retirement and settled in London. He was the owner of a Pakistani Restaurant and was also a member of the Finance Committee of the U.K Mission.</p>
                    <br />

                    <p>On my arrival in February, 1959 I was allotted a top floor flat at 61 Melrose Road.. We spent the first night at the flat most uncomfortably as there was no heating in the flat. It was very cold that night and at that time there was hardly any central heating in the houses. Normally coal fires or kerosene heaters were used for heating the houses but in our flat, we did not have either of these. We wrapped ourselves in the quilts that we had brought from Pakistan and even then, we hardly slept due to severe cold. In the morning, we went across to the Imam's residence for breakfast and he asked if we had spent our night in comfort. I told him that throughout my life, I had never been so cold during the night; even in Rabwah we used an electric heater. The Imam said:</p>

                    <div class="quote"> "There is no provision for these items in our budget."</div>

                    <p> In the afternoon when Moulvi Abd Ur Rahman came he heard our story of the bitter cold through the previous night. Since he was Secretary of the Finance Committee he confirmed that there was no room in the budget but he promised that he would take it upon himself to arrange a kerosene heater. He said that if there was any room in the future he would reimburse himself. In a short while, he brought an Aladdin kerosene heater and he taught us how to light it. Soon the room became cosy and we breathed a sigh of relief. May Allah grant him an exalted station in Heaven. I cannot ever forget this favour.
                        Maulvi Sahib was an enthusiastic member of the Community and made himself available whenever the Imam wanted him to do some duty. In those days neither the Mission nor many Ahmadis had their own motor cars. Fortunately Maulvi Sahib was the proud owner of a car which he would offer free of charge, whenever it was needed by the Imam or the Mission. </p>
                    <br />

                    <p>Maulvi Sahib was very popular among the Pakistani Community of U.K and had also established close relationship with the Pakistan High Commission. He had a very close and intimate relationship with Hazrat Chaudhri Sir Muhammad Zafrullah Khan. Whenever Sir Zafrullah Khan visited England he used Maulvi Sahib's car and enjoyed his hospitality.</p>
                    <br />

                    <p>I had the honour of being introduced for the first time to Sir Zafrullah Khan by Maulvi Sahib. Sir Zafrullah Khan was coming from The Hague, Holland and Maulvi Sahib was going to collect him at the Railway Station. He invited me to accompany him and receive Sir Zafrullah Khan along with him. We both received him and brought him to the Royal Commonwealth Society where he had booked his accommodation. Sir Zafrullah Khan asked me to meet him in the evening and to bring with me the latest issues of the "<strong>Daily Alfazl</strong>" for him. I met him in the evening and we talked about various things. He asked me to meet him every evening, if possible, as long as he was in London. </p>
                    <br />

                    <p>In January, 1960 I brought out the first issue of the monthly magazine called "The Muslim Herald." It was financed by me. The Mission had no financial contribution towards it at all. I desperately wanted some advertisements for the magazine so that it becomes self sufficient. I approached Maulvi Sahib for help in this connection as he knew many Restaurant owners and other firms. His response was most encouraging. In a few months' time I was able to get enough adverts for the magazine to make it self sufficient. It was all due to the selfless efforts and hard work of Maulvi Sahib. May Allah reward him abundantly. </p>
