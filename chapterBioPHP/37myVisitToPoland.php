                    <p>With His grace, the monthly periodical 'The Muslim Herald' started by me was being sent to several countries of the world. It had become a means of propagation of Ahmadiyyat and Islam. In certain countries, some libraries even subscribed to the periodical. I received very many letters from the readers every day. I received a letter from Imam Zook who was an Imam of a Mosque and a leader of the Muslims in Warsaw (Poland). He said that he had seen a copy of 'The Muslim Herald' and was deeply impressed by its contents. He asked for some more literature to be sent to him. He asked for his name to be added to the list of subscribers to 'The Muslim Herald'. He admitted that he did not know much English but a dear friend who lived in the adjoining flat knew English really well. He said that his friend had already translated various articles in the periodical into Polish for him. He also said that his friend was interested in Islam. I added Imam Zook's name to the list of subscribers and sent him some literature.
                        Fortunately, around that time, through the Rotary Club, I met an Englishman, Mr Mark, who was well versed in the Polish language. He promised that he would translate the letters from Imam Zook for me. That is how Imam Zook and I started corresponding with each other in Polish. For my benefit, Mr Mark would translate the letters written to me in Polish. This state of affairs continued for a while and slowly Imam Zook was introduced to Ahmadiyyat. A little later he invited me to visit Poland and said that on my arrival he would arrange for some Christian scholars and me to meet some Muslim. I forwarded Imam Zook's letter to Hadhrat Khalifa tul Masih III who instructed me to take advantage of this opportunity.</p>
                    <br />

                    <p>Therefore I travelled to Warsaw on 20th may 1976 and was received by Imam Zook at the Airport. I stayed at the Grand Hotel and the following day we had a long detailed discussion at his residence. His neighbor, who knew English well acted as an interpreter. Our meeting continued late into the night. Earlier on Imam Zook had prepared a lengthy list of questions that he wished to ask. With Allah's Grace I was able to answer all his questions and my answers seemed to satisfy him. In the evening, in the company of the Imam and his interpreter friend, I went sight seeing. He for the most part he showed me that section of Warsaw, which had been destroyed by Hitler during the Second World War. There was no trace of any of the old buildings.</p>
                    <br />

                    <p>However, at the end of the war the residents of Warsaw collected donations and restored that part of the town to its former glory. As a result there was no trace of a war on any of the buildings. I was deeply impressed and felt convinced that if any people rise with determination no power on earth could annihilate them.</p>
                    <br />

                    <p>After a few days discussion Imam Zook accepted Ahmadiyyat. He filled the Ba'it form and signed it. At that time Imam Zook was one of the six Imams in Poland. Since he was the Imam of the Mosque in Warsaw and was a learned person, he was considered more important than the others were. He was born in a Christian household. He came across a copy of the Holy Quran when he was studying in an academy to be a Christian priest. On the study of the Holy Quran, he accepted Islam. In this way, instead of a cleric he became Imam of a Mosque and later on the Almighty directed him towards Ahmadiyyat. Imam Zook had the good fortune to visit Rabwah in 1978 where he had the honor of meeting Hadhrat Khalifa tul Masih III.</p>
                    <br />

                    <p>By the Grace of Allah the Ahmadiyya Jamaat has now been regularly registered in Poland and a Central Missionary is posted there.</p>


