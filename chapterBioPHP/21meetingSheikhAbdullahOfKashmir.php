                    <p>After having been released from prison where he had spent many years, Sheikh Muhammad Abdullah came to England on a tour. There was considerable excitement and enthusiasm amongst Pakistanis and particularly amongst the Kashmiris. Reception Committees were formed in most big towns and they had chalked out detailed programs to welcome the Sheikh. I had a lot of respect for the Sheikh. I was deeply impressed with the manner, in which, without caring for his life or his possessions, merely for the sake of the freedom of the Kashmiris, without the slightest hesitation, he had borne the hardships of prison for lengthy periods. It appeared that he had devoted his entire life for service to his own people. I too had devoted my life but for another purpose. I knew that in accordance with the advice of Hadhrat Khalifa tul Masih II, the Sheikh had made great efforts to rid the Kashmiris of the claws of the Dogra Raj and had put in hard work for their freedom. Over the years, I had read in the 'History of Ahmadiyyat' some letters that the Sheikh had written to Hadhrat Khalifa Tul Masih II Mirza Bashir ud Deen Mahmood Ahmad.</p><br />

                    <p>In one of his letters he had said:</p>

                    <div class="quote"> "First of all I regard it as my duty to thank you from the bottom of my heart for your selflessness, without any expectation of a reward, the services you have rendered in the struggle for the downtrodden Muslims. In view of the constant and uninterrupted work that you have done to solve the Kashmir problem, I fondly hope that, like in the past, in the future as well, you will remain engaged in the struggle".</div>

                    <p>In letters of this kind the Sheikh had recorded his admiration for the Jamaat Ahmadiyya in general and for Hadhrat Khalifa tul Masih II in particular, who, as Chairman of the Kashmir Committee had worked so hard for the Kashmiris.</p>
                    <br />
                    <p>When I heard that the Sheikh was in England, within me, I found an overpowering desire to meet this fighter for the freedom of his people who had, for them, cheerfully borne the hardships of prison. In several places, I tried to contact him by telephone. He was so preoccupied that I was unable to establish contact with him. I did not give up and finally I was able to speak to him. I told him that I was the Imam of the Fazl Ahmadiyya Mosque and the Ahmadiyya Missionary in Charge in Great Britain. I said that I was desirous of meeting him.</p>
                    <br />

                    <p>The Sheikh seemed very pleased that I was able to contact him. He said that he also wanted to contact the Ahmadiyya Jamaat in England. He said that the organizers had framed his program in such a way that there was no time to spare and was involved in an unending chain of speeches and meetings. He also said that the Indian CID had been in his pursuit all the time and kept a strict eye on those who met him. He said that, in a few days time, we could meet and talk. After a few days he told me that he would be in London to deliver a speech. He asked me to come to the Hall where he was to speak half an hour before commencement of proceedings. He directed me to come straight to the stage. I did as directed.</p>
                    <br />

                    <p>At the entrance of the stage, with orders to escort me to him, the Sheikh had posted two young men. Dr. Sardar Nazir Ahmad was also with me. His close associates such as Mirza Afzal Beg and some other prominent Kashmiris who lived in England surrounded the Sheikh. The Sheikh started by enquiring about the health of Hadhrat Khalifa tul Masih II. I had to tell him that he was unwell and there was cause for concern. Tears trickled down the eyes of the Shaikh, which he wiped with his handkerchief. Then he said:</p>

                    <div class="quote">"You must be writing letters to him."</div>

                    <p>I said that I wrote to Hadhrat sahib every week. The Sheikh asked me to convey his affectionate message of adoration to Huzoor. He also asked me to convey to him that in the previous ten or twelve years he had been imprisoned constantly and therefore had not been able to write to him. However, even for a moment, he had not forgotten Huzoor and there was never any weakening in his prayers for him. </p>

                    <p>Then he said to me:</p>

                    <div class="quote">"You cannot possibly imagine how deeply I am indebted to Hadhrat Mirza Sahib. He has trained me like his own child and he stood besides me whenever I had any problems. He always encouraged me and guided me. Had it not been for his leadership I would not have been where I am today."</div>

                    <p>Then he mentioned in some detail what Huzoor had done for the well being and freedom of the Kashmiris. The conversation continued for quite a while and then he enquired about Hadhrat Maulana Abd ur Raheem Dard. I said that he too had passed away. Then he enquired about Hadhrat Syed Zain ul Abid e Deen Wali ul Ullah Shah and I had to tell him that he too had departed. Once again, he had tears in his eyes and for a few minutes, he explained the manner in which these two had faithfully served the cause of the Kashmiris.
                        It was now time for the meeting to begin. The hall was full to its capacity and slogans of Sheikh Abdullah Zinda Baad were constantly being raised. He asked me to occupy a chair on the stage alongside some other Kashmiri leaders which I did.</p>
                    <br />
                    <p>A few years later I visited Kashmir in December .I was received by Our Missionary stationed at Srinagar at the airport along with some other Ahmadis.He told me that he had informed Sheikh Abdullah, who was then the prime Minister of Kashmir, of my forthcoming visit. Due to winter the Government departments and the Prime Minister himself had shifted to Jammu. He instructed the missionary to convey his Salaam to me and to ask me to see him at Jammu, if possible, as his guest. Unfortunately I had no time to visit Jammu and meet the Sheikh. However I thanked him for his kind invitation.</p>


