                    <p>On the walls of that room Kalima Tayyeba and some Quranic verses had been inscribed. He stressed the need to pay particular attention to Tableegh in Brighton. On my return to England, I made up my mind to set in motion Tableegh work in Brighton. Mr Abdul Aziz Deen and I went to Brighton and rented a room in the Royal Pavilion. </p>
                    <br />

                    <p>Announcements were made in the local papers that from the following week, every week, speeches on Islam would be delivered in that Hall.
                        A local paper interviewed me and published a detailed account and my photograph. We printed 5,000 copies of a handbill that were distributed in the residential areas. During the first meeting, the Hall was filled to capacity. The meeting was presided over by Abd ul Aziz Deen. Moulvi Abdul Kareem and Moulvi Abd ur Rahman also attended the meeting. I spoke on the subject of 'The Truth of Islam'.</p>
                    <br />

                    <p>After my speech, a Christian cleric invited me to a debate, an invitation that I could hardly turn down. It was agreed to hold a debate on the subject of 'Death of Jesus on the Cross' on the following Saturday. The local papers published the news of the approaching debate. To attend the meeting many Ahmadi friends from London came to Brighton. A large number of British non-Muslims were also present. In fact they formed the overwhelming majority. There was considerable enthusiasm apparent amongst the audience.</p>
                    <br />

                    <p>We all waited for the cleric but he did not turn up. It appeared that he had no intention of coming. Disappointed, I got up to speak on the subject of 'Islam'. As the cleric had shied away from the debate, from our point of view, the local British audience seemed to have gained a positive impression.
                        At the end of the next meeting a Miss Irene Crene joined the fold of Ahmadiyyat. Several of her articles were published in the 'Muslim Herald'. Later on, she migrated to Turkey.</p>
                    <br />
                    <p>In the course of three or four meetings, six or seven other English women and men also joined Ahmadiyyat. This way a regular Jamaat was established in Brighton. This outcome was a direct result of the attention and prayers by Hadhrat Khalifa tul Masih II. For two years, this routine was followed every week. When I was appointed as Imam of The London Mosque, because I was alone and there was no other Missionary who could be sent to Brighton, most reluctantly, we had to give up this practice.</p>


