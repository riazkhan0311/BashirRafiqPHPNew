                    <p>Abdul Qayyum Khan played a very important role in the history of Pakistan. Before partition, he was Deputy Leader of the All India Congress Party. On the formation of Pakistan, he became Chief Minister of the Frontier Province. For many years, he ruled the Frontier Province with an iron hand and great awe. He introduced some revolutionary reforms and firmly established the province on the road to progress. Then he took charge of the Commerce and Industry Ministry in the Central Government. He also had charge of many other ministries. He became the Home Minister in the Zulfiqar Ali Bhutto's Government. He was a fiery orator. He could speak in Pushto, Urdu and English with equal fluency. On many occasions, Hadhrat Chaudhry Muhammad Zafrulla Khan paid tributes to his mastery of the English language.</p>
                    <br />

                    <p>In 1953 during the Anti-Ahmadi Movement, the province of the Punjab was engulfed in bloody disturbances. However, through his political practical skill, in his province, the Khan dealt with the situation very firmly and did not permit any disorder to take place. As a result, although it was the centre of some Mullahs who were known for spreading anarchy, the Frontier Province remained immune from disturbances. The life and possessions of all Ahmadi remained unaffected.</p>
                    <br />

                    <p>In 1967, an Urdu newspaper in London published the news that Khan Abd ul Qayyum Khan was to visit England. I obtained his telephone contact from the Pakistan High Commission and on the phone; I introduced myself as Imam of the London Mosque. I also told the Khan that I was a nephew of Khan Sameen Jan, a former Minister for Education and Prisons in the Frontier Province. My uncle, apart from being a well-known leader in the province had for four years served as a Minister in the Muslim League Government. Khan Abdul Qayyum Khan expressed his pleasure at my having welcomed him and when I expressed my desire to meet him, he said that I could come anytime.</p>
                    <br />

                    <p>The following day when I went to see him, he received me with warmth and love. We talked a lot and it would suffice to sum it up as follows. The Khan said:</p>


                    <div class="quote">"I consider the Jamaat Ahmadiyya and particularly Hadhrat Khalifa tul Masih II, Hadhrat Mirza Bashir ud Deen Mahmood Ahmad, praiseworthy. I was deeply impressed by Huzoor and regarded him as a great political leader of India. Although he was a religious leader, in the field of politics he had no peer. In 1924 when I was a student at the London School of Economics, I read in the papers that Mirza Sahib was soon to tour England. I contacted your Jamaat at Putney and I was told the date on which Mirza Sahib would arrive. On that date, I took some Indian students along with me and went to the Victoria Station where the boat trains were to arrive. There the Imam of the Mosque and some seniors of the Ahmadiyya Jamaat in England were present. Many English dignitaries had also come to receive him. We joined one of the welcoming delegations and when the train came to a stop at the platform and Mirza Sahib emerged, I raised the slogan of 'Takbeer'. The Indian students joined me in my slogan and thus Victoria Railway Station resounded with our slogans. I then presented myself before him. After enquiring about my circumstances, he tendered valuable advice and counsel. Even in those days in India, your second Khalifa was well known as a political leader. We the Indian students regarded him not only a religious leader but also as a great statesman.</div>

                    <p>In 1953 when the whole of the Punjab Province was engulfed in disturbances against your Jamaat, I was Chief Minister of the Frontier Province. I was well aware of the anti Pakistan deeds of Majlis e Ahrar. I knew very well that this uprising was in fact of a political nature and for the achievement of political objectives; it was cloaked in a religious garb. Therefore, I said to myself 'come what may, I would protect the lives and property of the Ahmadi Muslims in the Frontier Province and that I would, with an iron hand, suppress any movement that creates disorder'. Accordingly, I held meetings up and down the whole of the Frontier Province and during my speeches warned the Mullahs that whoever tried to take the law into his own hands will have me to contend with and would be dealt with most severely. I banned the entry of some papers published in the Punjab that were supporting the Mullahs. I imprisoned those Mullahs from whom I feared disturbances.</p>
                    <br />

                    <p>He said that in those days the Amir of our Jamaat in the Frontier Province, Qazi Muhammad Yusuf, leading a delegation called on him. He returned with an assurance that the life and property of every Ahmadi in the Frontier Province was his own responsibility. Therefore, they need have no worry.
                        When I heard this detailed account, I thanked the Khan sincerely and submitted that I would convey a detailed account of this meeting to Khalifa tul Masih III.</p>
                    <br />

                    <p>The Khan paid a glowing tribute to Hadhrat Chaudhry Zafrulla Khan and said:</p>

                    <div class="quote">"He was my role model in the field of politics. I must admit that I have not met another politician who was so honest, principled and self-respecting. After Qaid e Azam, he was the greatest leader in Pakistan. However, it is a pity that our nation did not look upon him in that light."</div>

                    <p>Then I invited the Khan to visit the Mosque. He very kindly accepted the invitation but suddenly he had to leave for Pakistan the next day and he tendered his apologies over the phone.</p>


