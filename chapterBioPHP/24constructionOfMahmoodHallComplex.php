                    <p>Our plans were approved by Hadhrat Khalifa tul Masih III and then we set about obtaining approval from local authorities. When we were ready for signing the Agreement, suddenly and without any prior notice the Building Society withdrew their offer to provide the necessary funds. Our endeavors during the previous year went to complete waste. The day following the refusal by the Building Society, from Holland, Hadhrat Chaudhry Zafrulla Khan came to London. He stayed with me and during our conversation over dinner; I said that the Building Society had refused to finance the project. Since I was disheartened and disappointment I spoke with great anguish. I said that a whole year's efforts of the Building Committee had been wasted and that our dream of providing a spacious, beautiful and comfortable Mission House had come to nothing.</p>
                    <br />
                    <p>At that time, Chaudhry Sahib kept quiet and that was the end of the matter. During the following week, when Hadhrat Chaudhry Sahib came back to London he again stayed with me. At dinner he asked that if he personally financed the building of the new Mission House on the same terms as the Building Society would the Centre approve? I said:</p>
                    <br />

                    <div class="quote">"What else could the Centre possibly want? If your offer is firm I will write to Huzoor immediately for his approval."</div>

