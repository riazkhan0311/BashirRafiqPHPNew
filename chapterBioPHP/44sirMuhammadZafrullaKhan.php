                    <p>For the first time in my life, during my student days, while in Qadian during a Majlis Irfaan held by Hadhrat Khalifa tul Masih II in the Masjid Mubarak in Qadian, I saw Hadhrat Chaudhry Sahib sitting on a chair next to Hadhrat Khalifa tul Masih as the rest of us sat on the floor of the Mosque.</p>
                    <br />

                    <p>In 1953, I passed my Degree Examination from the Taleem ul Islam College in Lahore. Hadhrat Chaudhry Sahib, who was then Foreign Minister of Pakistan, presided at the Convocation Ceremony. After having delivered a speech he awarded the Degrees and I received one from his blessed hands. A photograph was taken at the end of the ceremony. In it Hadhrat Sahibzada Mirza Nasir Ahmad, the then Principal of the Taleem ul Islam College stood in the middle. The students who had been awarded their Degrees, wearing their gowns, surrounded Hadhrat Chaudhry Sahib. The memory of that day is impressed deeply on my mind. I was so happy at the award of a Degree from the hand of such an outstanding personality.</p>
                    <br />


                    <p>In 1959 when I was Deputy Imam of the London Mosque, Mouvli Abd ur Rahman, proprietor of the restaurant at the Pakistani Students hostel told me that Chaudhry Sahib was due to arrive in London by train from The Hague. He said he was going to the railway station to receive him and invited me to accompany him; an invitation that I could not refuse. We received Hadhrat Chaudhry Sahib at the railway station and went straight to the Royal Commonwealth Society. As he was a member of the society, he normally stayed there. Having been sponsored by him I also remained a member of the Royal Society for many years.</p>
                    <br />

                    <p>The two of us, along with Chaudhry Sahib, settled in the spacious drawing room of the society. Chaudhry Sahib asked me at what time the Fajar Prayer was offered in London. He told me that when I came to see him the following day I should bring him the recent 'Daily Al Fazl' issues. When I called to see him the next day I took with me the few issues of the 'Al Fazl' received during the preceding few days.</p>
                    <br />

                    <p>I was asked to properly introduce myself and I did exactly that. I was fortunate to spend some time with him. I was exceedingly happy and considered that day to be a most auspicious one as I was enabled to spend time in the company of a Companion of the Promised Messiah, who was, in his own right, a famous and outstanding person.</p>
                    <br />

                    <p>During Chaudhry Sahib's next visit to London, I accompanied Moulvi Abd ur Rahman to the Liverpool Railway Station when he went to receive him. On that occasion, apart from advising Moulvi Abd ur Rahman of his visit he also wrote a letter to me and asked me to bring along with me some recent issues of the 'Al Fazl'. That was his very first letter to me. During the second visit, he stayed in London for two days during which I was with him most of the time. It became apparent to me that it was also his desire that I should spend time with him. That was the foundation of my association with him, which continued for 14 or 15 years. Throughout that period, right until the end, I remained recipient of his kindness, his affection and his countless favours. He was like a kind, indulgent and affectionate father to me. The following will exemplify this relationship. When I once asked him to pray for me, he held my hand and said: </p>

                    <div class="quote">"Imam Sahib, there is no need for you to ever remind me to pray for you as every single day, without fail I pray for Khanum (my wife), her husband and her children."</div>

                    <p> This was immense good fortune for me.
                        Apart from him, there is another individual who, in a way, was a son to him and who, in his youth, had been brought up by him in his care. That person is my brother, Anwar Ahmad Kahlon, who also, on very many occasions, has told me that there is no need for me to remind him for prayers as he had permanently included me and my in the list of those for whom he prays regularly. He told me that, by naming each, he always prays for Saleema Begum, all my children, their spouses and me. I am convinced that this habit grew out of his upbringing under the influence of Hadhrat Chaudhry Sahib who, in a way, was his parent.</p>
                    <br />

                    <p>(Both of us have written books concerning the life and character of Hadhrat Chaudhry Sahib. My book is in Urdu and Brother Anwar Ahmad Kahlon's book is in English, which is worth reading. How I wish his book could be translated into other languages, particularly Urdu, so that on its perusal, the readers will have their faith enhanced.)</p>
                    <br />

                    <p>After retirement from the International Court of Justice Hadhrat Chaudhry Sahib became a permanent resident of the top floor flat of the Mission House. At that time I occupied the first floor. During this period of 8 or 9 years, I was blessed with repeated occasions of being very close to him. It was our good fortune that, every day, he had all his meals with us. My wife took good care to serve him the kind of dishes that were suitable for his health. After dinner, Chaudhry Sahib would habitually talk about the current events and certain occurrences in his earlier years. For me his talk was like lessons provided in an academy. I learned a lot at the dinner table.</p>
                    <br />

                    <p>Here I consider it appropriate to repeat what Brother Anwar Ahmad Kahlon has written about the manner in which my wife took care of Chaudhry Sahib.</p>

                    <div class="quote">"From February 1973 until September 1979 while Baba Ji lived in the caretaker's flat, Imam Bashir Ahmad Rafiq and Khanum assumed responsibility for caring for and feeding him. Although Baba Ji ate simple meals, he had to eat a well-regulated diet at well-regulated hours. Throughout this period Khanum, the wife of the Imam, took special care to serve to him only what was good for him, and at the appointed times. This was no easy task, as the Rafiqs were a very hospitable family and she had to cater for many other guests. During his visits to England, Hadhrat Khalifa tul Masih III always stayed in the Imam's flat. The Imam and his family squeezed themselves into the library. Nevertheless, Khanum remained responsible for hospitality and care of all the guests. During such periods, one of the Rafiq children took up Baba Ji's meals to the caretakers flat on a tray. Baba Ji developed deep affection and love for the Rafiq family, particularly for Khanum whom he held in very high esteem."</div>

                    <p>As I was fortunate enough to have spent so many years in the exalted company of Hadhrat Chaudhri Sahib that I wanted to relate so many events that will easily fill a whole book. The readers can imagine what a great deal of material is available to me to write about. However, since I have already written about my remembrances concerning Chaudhry Sahib.</p>
                    <br />

                    <p>Chaudhry Sahib, I have now made a conscious effort not to repeat what is already included in my Urdu book. I do hope that the almighty Allah will create conditions so that the English translation of my book "Muhammad Zafrullah Khan- Chand Yadain" will be published.</p>


