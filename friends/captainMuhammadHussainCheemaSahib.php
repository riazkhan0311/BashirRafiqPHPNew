                    <p>In his later life he started memorizing the Holy Quran and was able to memorize a large portion of it. He attended the Tarjuman-ul- Quran classes of Hazrat Khalifatul Masih IV regularly. I had seen tears in his eyes quite often while reciting the Holy Quran. He used to do Ea'tikaf (retreat in the mosque during the last ten days of Ramadhan) and would recite the entire Holy Quran during the Ea'tikaf.
                        Captain Sahib was a pious, obedient and a righteous person. He had performed several Umras and Hajj and also used to visit Qadian every year. He had a vast circle of friends and found happiness in inviting them to his home for dinners. I was one of those who had enjoyed his hospitality on numerous occasions.
                        He had a great desire to be buried in Bihishti Maqbara (Heavenly Graveyard) at Qadian, India. As he was a Pakistani National it was not possible. He started lengthy correspondence with the Indian High Commission in the UK and requested them to give special permission for his body to be interned in Qadian. His request was denied which made Captain Sahib very depressed and sad. I tried to console him and said that Allah would grant him a place in paradise irrespective of the graveyard he was buried in.
                        In 1991, Hadhrat Khalifatul Masih IV visited Qadian for the first time after the partition of the sub-continent. It was a historic occasion. I had the honour and privilege of being a member of Hudhoor's entourage. On our arrival there I met Captain Sahib in the Mubarak Mosque. He was very happy to meet me and invited me to have tea with him the next day at his residence. He said he had bought some special bananas and tangerines and would also get some fresh fish to be fried at Daru-Ziafat (public kitchen). I gladly accepted his invitation and promised to be with him after Asar prayers the next day. But Allah had His own plans for Captain Sahib. He died early the next day and as he had wished all his life, was buried in the Heavenly graveyard, Qadian. His funeral prayer service was led by Huzoor. Allah saved him from begging the Indian Government to give permission for his remains to be buried at Qadian. Almighty Allah brought him to Qadian alive and then made him to die there to be buried there as he had wished. God is Great..</p>

