                    <h2>A Memorable Journey.
                        <br>
                        <small>1959</small>
                    </h2>
                    <p> From Karachi, we were to sail on the S.S. Caledonia that belonged to the Anchor Line. This vessel carried only one class of passengers. The vessel was to sail in the afternoon of Friday 23rd January 1959. After attending the Friday service at the Ahmadiyya mosque Karachi, along with my wife and my son Munir Ahmad I went to the Port. </p>
                    <br>
                    <p>At the time of departure of the ship, Dr. Saeed Ahmad Khan and Dr. Basharat Ahmad were also present who had come to bid us farewell. They were then students of Dow Medical College, Karachi. Mr Khaleel ur Rahman, Secretary Ziafat, led a solemn silent prayer and we boarded the ship. </p>
                    <br>
                    <p>Cabin No. B10, which was spacious and very comfortable, had been allotted to us. Apart from two beds, there was a cot for the child. Hot and cold running water was available in the Cabin, and through a porthole, one could see the ocean.</p>
                    <br>
                    <p>After leaving our baggage in the cabin we went up to the Deck. Those who had come to see us off remained on the shore until the ship had gone too far out to the sea. Anchors were weighed at 6.30 p.m. and very slowly we started moving into the open sea.</p>
                    <br>
                    <p>The setting was charged with affection and I was overcome with emotion. It was a touching scene to see not only those who were traveling but also those who had come to see them off shedding tears. Some sobbed openly and others were inconsolable.</p>
                    <br>
                    <p>My own condition was strange. Turbulent thoughts occupied my mind and I said to myself: </p>
                    <br>
                    <div class="quote"> “You have indeed dedicated your life for the service of Islam, but will you be able to carry out its demands? Will you be able to surrender totally your feelings, your life, your belongings and your honor for the sake of Allah? Will you be able to spend the rest of your life in complete subordination to the will of Allah?”</div>
                    <br>
                    <div class="quote"> "On the one side, as a Missionary of Islam I was weighed down by the assignment that was being entrusted to me, and on the other hand I was assessing the deficiency in my knowledge. Whilst thinking of my own spiritual weaknesses and insufficient good deeds my heart sank with apprehension. It was indeed a very peculiar state of affairs through which I was passing. These thoughts overwhelmed me and I began to shed tears. With great humility, I raised my hands for prayers and said: “My Lord, unless You show Your Mercy and Your Grace and be my constant Helper, I will find the responsibility entrusted to me very hard to bear. I fear that I cannot possibly discharge it adequately. My Lord, this worthless person has been entrusted with lofty responsibilities. I am likely to fail unless through Your Grace I am helped and guided constantly. Therefore, my Lord, help me and shelter me. With Your attribute of ‘Coverer of faults’, please cover and conceal my faults and help me through “Roohulqudus, (the Holy Spirit).”</div>
                    <br>
                    <p>After such lengthy supplications when I looked around, I could see nothing but water. The shore was no longer visible. As the ambient temperature had dropped considerably, we went back to our cabin. My wife, Salima was so weighed down with emotion that she seemed below par.</p>
                     <br>
                     <p>This was our first journey taking us across the Seven Seas. The parents of both of us were alive and they had, with tears in their eyes, bid us farewell. By remembering them both, we attempted to warm our hearts. In those days, many who traversed the Seven Seas never returned. Many adopted European customs and settled in the West. Therefore, naturally, the relatives of those who embarked on journeys to Europe became extremely apprehensive.</p>
                     <br>
                     <p>My mother had a heart condition. I was her eldest son. At the time of my departure from Rabwah, perhaps due to the prospect of a lengthy separation she collapsed. I was consumed by her thoughts. Then I recalled what my father, just before my departure at Faisalabad Railway Station had said to me. With a halting voice, full of emotion, he said:</p>
                     <br>
                        <div class="quote">“According to your own option you have dedicated your life. No one pressurized you into this vocation. Now you will have to abide by the Covenant that you had made with the Almighty. Drifting away from your Waqf, due to some problems or trials, apart from resulting in a spiritual death, is wholly contrary to Pakhtoons traditions. God forbid, if you ever break your Covenant with God, remember that from then on that will also be the end of my relationship with you. Do not then turn to me, as, from that day, for me you will have died. Moreover, if I ever found out that influenced by the European way of life, your wife ceased to observe Purdah, then I will have nothing to do with either of you.”</div>
                     <br>
                     <p>After resting a while in the cabins all the passengers assembled in the Dining Room for the evening meal. I discovered that at various tables ladies and gentlemen, all mixed together, were seated. Of all the women on the ship, my wife was the only one who observed Purdah. Most women were Europeans and the remaining Asian women regarded Purdah as a ludicrous ancient custom. </p>
