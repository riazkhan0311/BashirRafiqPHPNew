                    <p>A good teacher can be counted as one of the greatest gifts from the Almighty. If one has a good teacher one's whole life can get packed with numerous blessings. Prophets and saints who bring about a spiritual revolution are also teachers. They leave behind thousands of pupils who in turn spread profound knowledge throughout the world.
                        I count myself most fortunate that, through Allah's grace, I had teachers who, with their distinguished manners and vast knowledge have left an indelible mark on me. One such teacher was Hadhrat Maulana Abul Ata.</p>
                    <br />

                    <p>The author with Hadhrat Maulana Abul Ata
                        Hospitality was the most commendable of the Maulana's attributes. Repeatedly, even when I was a student, I was beneficiary of his hospitality. He did not enjoy a meal by himself. Once, in Rabwah when I was on a visit from London just for one week, the Maulana was away on a tour. However, he returned a day before I was due to leave. I went to pay my respects and he asked me to join him for dinner the following evening. I had to apologize as I already had an engagement for dinner. He asked me for lunch and once again, I had to say that I had another appointment for lunch. Then he asked me to join him for breakfast. It was much more than breakfast. Roast lamb, kebabs, chicken, eggs, parathas, junket, yoghurt and tea were laid on the table. He persisted that I should eat everything.</p>
                    <br />
                    <p>I never visited his office without having been given tea. Once I called on him in the summer months. Immediately on my arrival, he pushed a bucket towards me in which there were mangoes dipped in crushed ice. He told me that since he had received the mangoes as a gift he had called me so that I could join him.
                        When I was Huzoor's Private Secretary the Maulana once came to my office and told me that I must show hospitality to the visitors who come from great distances to meet Huzoor. He said that tea and cold drinks should be served as a routine. I said that there was no room for the beverages in my budget. He kept quiet and went upstairs to meet Huzoor. There was a great expression of happiness on his face when he returned. He said that he had told Huzoor that there was no provision in the budget of the Private Secretary for hospitality towards those who come to see Huzoor. Huzoor put his hand in his pocket, brought out a pile of notes and instructed that arrangements should be made for tea, cold drinks and biscuits for visitors on a daily basis. He directed that whenever necessary he might be approached for more cash.</p>
                    <br />
                    <p>The Maulana gave me the cash and I set in motion hospitality on the lines indicated. The Maulana continued to give me whenever he received more money from Huzoor. I was by no means the only one who benefited from his hospitality; hundreds had the same experience.</p>
                    <br />
                    <p>As the Maulana himself was an ocean of knowledge personified, he wanted his pupils also to delve deep into the ocean of knowledge. Even before I entered the Jamia, I was fond of writing articles. I had discharged duties as Editor of the Urdu section of the College Magazine 'Al Minar'. However, I did not write on any educational subjects. During my first year in the Jamia, the Maulana instructed me to write articles for the periodical 'Al Furqan'. With his constant encouragement, when I wrote two articles, he said:</p>


                    <div class="quote">"Keep on writing and do not worry if they are not considered good enough to be published in 'Al Furqan'. It is my job to make your articles worthy of publication."</div>

                    <p>Thereafter my articles continued to be published in both 'Al Furqan' and 'Al Fazl'. That was made possible only because of the Maulana's specific attention. When I came to London, he continued to insist that I should send him articles. After a while, to encourage me, he included me in the Editorial Board of the periodical 'Al Furqan'. Therefore, amongst the names of other members of the Editorial Board, for a period of four or five years, my name also appeared on the title page. He was a dazzling luminous lamp himself. From him many other lamps continued to be lit. By no means was he a dry scholar and indeed he had a wonderful sense of humor. He would be thrilled on hearing pure jokes and would often tell some jokes himself. I felt that whenever I was with him and for a while even after I departed I continued to be full of cheer. Once, during a lesson the Maulana said:</p>


                    <div class="quote">"I like those pupils who establish an innate personal and unceremonious relationship with me."</div>

                    <p>He added:</p>


                    <div class="quote">"We the pupils of Hadhrat Maulana Roshan Ali Sahib did not stand on any ceremony with him and we even borrowed money from him. Without asking any questions the Maulana would give us cash."</div>

                    <p>Three or four days later, I asked Maulana Abul Ata for a loan of Rs.10. After paying the amount to me, he asked why I needed the money. I enquired from him as to whether Hadhrat Maulana Roshan Ali also asked such questions. He had a hearty laugh and asked if I had approached him to put him to a trial. When I attempted to return Rs.10, he said that I could use the sum for tea etc.</p>
                    <br />

                    <p>There are so very many that I cannot choose which favors to recall. He had been endowed with a handsome, attractive face and his physique was proportionate. He would always wear neat and clean clothes. He took care to wear spotlessness clothes and kept a watchful eye on the clothes of his students. If he ever saw any student wearing soiled or grubby clothes, in an appropriate manner, he would tell the student that cleanliness is an important part of the faith. Never did I see him in soiled or creased clothes. He paid particular attention to our training and guidance. He directed that, every day, all of us, should offer all our Salaats in the Mubarak Mosque behind Khalifa tul Masih II and that we should regularly participate in the discourses of Huzoor. If he found any student absent, he would send for him and in an inspiring manner explain to him the benefits of attending the Majlis e Irfaan. In those days, after Asr, Huzoor used to hold an assembly for quite a while. Once, after such a session, Huzoor asked me to deliver a speech in Arabic the following week. As my knowledge of Arabic was strictly limited, I froze. After the session, the Maulana summoned me and told me not to worry. He said:</p>

                    <div class="quote">"Come and see me."</div>

                    <p>He dictated to me my speech and asked me to memorize it thoroughly and then, initially, to deliver it in his presence. He said:</p>


                    <div class="quote">"Don't be afraid. This is the only way you will learn Arabic."</div>

                    <p>Then he related an incident from his own life. When he was very young, a mere student of the Madrisa Ahmadiyya, he was sent to debate with Reverend Abul Haq. Because of supplications, he became victorious. He often said that for success the very first condition is prayer and that if a student or missionary who does not remain engaged in prayers he can never succeed.</p>
                    <br />
                    <p>Seeing his students in the field gave him great satisfaction. When he heard that the President of Liberia had invited me to Liberia as his personal guest he became ecstatic. He insisted that I should send him photographs taken with the Liberian President so that he could publish them in the periodical 'Al Furqan'. He did publish some photographs and sent me a loving and a reassuring letter.</p>
                    <br />
                    <p>I will now give an illustration to show how, on learning what the Khalifa desired, with affection and utter devotion; he promptly took the necessary steps to implement the task. When Hadhrat, Khalifa tul Masih II, inaugurated Waqf e Jadeed and invited members of the Jamaat to volunteer as devotees of life, the Maulana offered all of his sons for service to the Jamaat. The Almighty blessed his sacrifice. One of his sons was enabled to serve the Movement in Liberia for very many years and another son became Imam of the London Mosque. One of his sons in law was given the title of 'captor of a realm' while the other son in law, became Sadar of the Majlis Ansarullah in the UK</p>


