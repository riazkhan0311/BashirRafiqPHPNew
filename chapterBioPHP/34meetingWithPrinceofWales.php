                    <p>The Prince of Wales was invited to participate and speak, an invitation that he kindly accepted. There were about 30 participants in the seminar. Amongst them were Ambassadors of some Muslim countries, senior officials in the Foreign Office and some intellectuals. Participants sat around a very large Round Table. As I had also been invited to attend the conference, on 13th December I reached Wilton Park Station by train, where along with other participants I was met. We were all given distinctive badges to wear. At 10 o'clock, a Helicopter carrying the Prince of Wales landed in the Park. All participants stood in a row. The Prince shook hands with every one. Each participant introduced himself. The participants stood around the Prince for a short while when some Islamic subjects came under discussion. The Prince said he was happy that the Islamic teachings were now beginning to be better understood and that the old prejudices were subsiding. In an informal manner, all participants sat around the Round Table. Alongside the Acting High Commissioner of Pakistan, I sat opposite the Prince. On the other side sat the Saudi Ambassador.</p>
