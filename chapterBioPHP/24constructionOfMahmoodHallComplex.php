                    <p>When the building work had been completed we were confronted with another problem i.e. provision of curtains, carpets, furniture and other necessary furnishings. The agreement with Chaudhry Sahib was confined to the construction of the building. For assistance in the matter of provision of these items I sought assistance from Chaudhry Sahib. He said:</p>
                    <br />

                    <div class="quote">"This was not included in the agreement. However, where so much money has been spent, I might as well finance the purchase of these items as well."</div>

