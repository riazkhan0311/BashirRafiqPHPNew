                <div class="chapterContent">

                    <p>In February 1953, while I was a student in my last year at college agitation against the Ahmadiyya Jamaat erupted all over the Punjab, particularly in Lahore. Primarily the 'Ahraari' Jamaat were responsible for the turmoil. They seemed to have complete support of the Government of the Punjab. A secret pact seemed to have been concluded with Mian Mumtaz Muhammad Khan Daulatana, the then, Chief Minister in the Punjab. Apparently, it had been agreed that the Government of the Punjab would render financial assistance to those newspapers and periodicals that published inflammatory articles and statements against the Jamaat as well as activities of the Ahraari Jamaat. It had been further agreed that the government would not take any action or arrest miscreants responsible for disrupting law and order.</p>
                    <br />

                    <p>Mian Mumtaz Muhammad Khan Daulatana belonged to a wealthy land owning family. His father, Mian Ahmad Yar Khan Daulatana had a close relationship with Hadhrat Khalifa tul Masih II. Mian Mumtaz was up at Oxford at the same time as Hadhrat Sahibzada Mirza Nasir Ahmad and they had become good friends. On the formation of Pakistan, Mian Mumtaz Daulatana was elected as the Chief Minister of the Punjab, the principal province of the country. He had the support and backing of the land owning families of the Punjab. He had a very strong ambition to embark on a task, which would help him to eventually become the Prime Minister of the whole country. It occurred to him that, for the achievement of this goal, he should secure the help and assistance of the 'Ahraar' who were perpetual opponents of the Jamaat Ahmadiyya.</p>
                    <br />


                    <p>This is a long story and is not directly linked to my life story. What I really wish to say is that at that point Mian Mumtaz Daulatana was at the summit of his political career. The Punjabi sphere seemed too small to him and he had his eye on the Prime Minister ship of Pakistan. He seemed prepared to sacrifice every principle for the achievement of this objective. However, the Almighty had other plans. Although during the 1953 Anti Ahmadyya disturbances the lives and belongings of Ahmadis did suffer, nevertheless, collectively, by the Grace of Allah, the Jamaat emerged relatively unscathed. Once again, the Jamaat started marching on the highway of progress. Instead, these disturbances not only shattered Mian Mumtaz Daulatana's dreams in the political field but also destroyed the Ahraari influence. The one who had visions of becoming the Prime Minister of the country even lost his position as Chief Minister of the Punjab. This was tantamount to a political demise.</p>
                    <br />

                    <p>In the decade of the 1970's, Mumtaz Daulatana was one of the top most leaders of the Muslim League. When Zulfiqar Bhutto rose to prominence, in order to get him out of the way, Bhutto offered him the position of High Commissioner in London, a position that he could not refuse. The news that soon Mumtaz Daulatana would assume charge as High Commissioner for Pakistan in England was published in all the papers. </p>
                    <br />


                    <p>In those days the Jamaat Ahmadiyya in the UK had a very cordial relationship with the Pakistan High Commission. At my invitation, one after the other, several High Commissioners had visited the Mosque. In turn, I was invited to participate in all the High Commissioner's functions.</p>
                    <br />

                    <p>When I read the news in the papers concerning Mumtaz Daulatana's appointment I feared that because of his attitude towards the Jamaat during the 1953 disturbances, we may not be able to continue our cordial relationship with the Pakistan High Commission. In view of this situation, I wrote to Hadhrat Khalifa tul Masih III and asked if we should maintain relations with Mian Mumtaz Daulatana. Huzoor replied that the Almighty had already punished Mumtaz Daulatana enough for his contribution to the 1953 disturbances. Huzoor said that since we neither oppose nor hate anyone we leave the consequences in the hands of the Almighty. Huzoor permitted me to establish a relationship with Mian Mumtaz Daulatana. He further said that if Daulatana's attitude had undergone a change and if he was remorseful of his earlier performance there was no reason whatsoever to restrict our dealings with him.
                        Soon after Mumtaz Daulatana assumed charge of the Pakistan High Commission, I wrote to him congratulating him on his appointment as High Commissioner to the United Kingdom. I pledged complete co-operation from the British Jamaat e Ahmadiyya and I expressed a desire to meet him. Soon after the receipt of my letter, Mian Mumtaz Daulatana himself called me on the telephone. He thanked me for my letter and invited me to have tea with him.</p>
                    <br />

                    <p>I arrived at the Pakistan High Commission at the appointed time. He had made arrangements for tea and received me with considerable warmth. That surprised me. During our conversation he said that he was pleased that I had written to him and that it had resulted in a meeting. Otherwise, he said, he was going to contact me. He said that perhaps I knew that his father had a very close relationship with Hadhrat Khalifa tul Masih II and that his father always consulted Huzoor in every matter of any importance. </p>
                    <br />

                    <p>Due to Huzoor's far sightedness and complete understanding of the leadership in the political field in India his father regarded him as a great leader. Qadian was often talked about in the Daulatana household and there was a continuous exchange of presents. He further said that when it was decided that he should be sent to Oxford, his father took him along to see Hadhrat Khalifa tul Masih II. His father instructed him to sit at Huzoor's feet but Huzoor would not have him sit at his feet. Huzoor made Mumtaz sit next to him.</p>
                    <br />

                    <p>Addressing Huzoor my father said:</p>

                    <div class="quote">"Here is my only son who is going to a country beyond the seven seas. I have certain misgivings about the moral environment in that country and I fear that influenced by the irreligious and free atmosphere in England my son may put aside his Indian and Islamic values. Therefore I seek your prayers."</div>

                    <p>Huzoor promised to pray and then, addressing me (Mumtaz), he said:</p>

                    <div class="quote">"We have a Mosque in London. On arrival, please contact them and they will help you in every way possible. This very day I will send them instructions to this effect."</div>

                    <p>On arrival at London I went straight to the Ahmadiyya Mosque in Putney and was warmly received by the then Imam, Maulana Abd ur Raheem Dard.</p>
                    <br />

                    <p>Mian Mumtaz told me that he spent his first few days in London in a room in the Mission House. He said that Maulana Dard not only guided him with his advice and counsel but his observations regarding the British way of life were most helpful. He said that a few days later when he had to go to Oxford, Maulana Dard went along with him right up to the Hostel where he was to stay. The Imam invited him to spend his holidays with him in London. He also said that if he were in any need then he should contact him unceremoniously. Mumtaz Daulatana said that on several occasions, whenever he visited London, he stayed at the Mission House and became friendly with Mirza Nasir Ahmad who was a student at that time. He acknowledged that he could never forget the great favours that were bestowed on him by Hadhrat Khalifa tul Masih II. During his early days in England, he admitted he received enormous support from him.</p>
                    <br />

                    <p>I was not aware of what Mian Sahib told me and I must say, I was very surprised. It occurred to me to ask him how, in 1953, he had returned the great favors bestowed on him. However, I said nothing.
                        Before departure from the High Commission, I invited Mian Sahib to visit the Mosque an invitation that he readily accepted. After having fixed a date and time for his visit I came back.</p>
                    <br />

                    <p>At the appointed date and time, Mian Daulatana visited the Mosque where elaborate arrangements had been made for hot and cold beverages. On getting out of his car, he said that this was not the building in which he had stayed and that it looked newly built. I explained that he had stayed at 63 Melrose Road, which had since been demolished and a new building had been erected in its place. After tea, he expressed a desire to see the Mosque again â€“ which he did.</p>
                    <br />

                    <p>A meeting had been arranged in the Mahmood Hall, which was filled to capacity by members of the Jamaat. After recitation of the Holy Quran, I presented a welcome address.</p>
                    <br />

                    <p>Mr Daulatana began his response by telling the audience that he was at these premises to renew his sentiments of love. Then he repeated all the incidents that he told me about at the High Commission. He also made certain general observations concerning politics in Pakistan. In the end he said that his doors were always open and whenever there was a problem he may be approached.</p>
                    <br />

                    <p>This function was a great success and apart from the local English newspapers the 'Jang' of London also published the news. Some papers also published photographs.</p>


                    <p>As time went on my relationship with Mr Daulatana became closer and closer. I would often visit him in the Pakistan High Commission. Mr Hidayatullah Bangvi was then working as Second Secretary in the High Commission. He was a devout Ahmadi and had served in the Foreign Service for a long while. When I visited the High Commissioner he would also often join us. </p>
                    <br />

                    <p>A little later Hadhrat Khalifa tul Masih III was to tour England. When I advised Mr Daulatana of his visit, he insisted that he should be given an opportunity to hold a Dinner Party in Huzoor's honor. On his arrival I advised Huzoor of Mr Daulatana's desire to throw a party. Huzoor accepted the invitation.
                        Mr Daulatana hosted a well attended party at his residence. Apart from officials of the Pakistan High Commission, Hadhrat Chaudhry Zafrulla Khan and I were present. Separate Purdah arrangements had been made for the ladies. Hadhrat Begum Sahiba, my wife, and Mrs Dr. Abdus Salaam were also invited.</p>


                    <p>In 1974 when the so called scholars of Majlis Khatam un Nabbuwat began their onslaught on the Jamaat Ahmadiyya Mian Daulatana was then the Pakistan High Commissioner in London. He seemed deeply concerned about the events in Pakistan and on many occasions, he sent for me and gave me some details of the events. During one of our meetings, he said:</p>


                    <div class="quote">"How unfortunate is Pakistan as a peace loving Jamaat is being persecuted."</div>

                    <p>He expressed his regret for being in a position when, as the High Commissioner, he had to represent the Pakistan Government which was then supporting the Majlis Khatam un Nabbuwat and who seemed bent upon creating disorder.
                        Although he hailed from a very wealthy land owning family and had served as Chief Minister of the Punjab, in his dealings with me I found Mr Daulatana a courteous and a humble person. He always talked to me in Punjabi. On occasion, I would admit that being a Pathan my competence in the Punjabi language was strictly limited. In spite of that, he always talked to me in Punjabi.</p>


