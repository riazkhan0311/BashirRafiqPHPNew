                    <p>In 1960 the Pakistan International Airlines acquired their first Jet propelled Boeing. For their inaugural flight, they invited some Pakistani and British dignitaries from London to fly with them in the new Boeing. I also received an invitation. Perhaps in the month of April 1960, with permission from the Centre, I went to Pakistan for nearly a month. During my stay there, two or three times, I had the good fortune of an audience with Huzoor who, in those days, was unwell. In the course of a meeting Huzoor directed that we should pay particular attention to Tableegh in Brighton. He said that in 1924 when he toured England he visited Brighton at the invitation of the Brighton Council. During that visit he saw the room where, as guests of Queen Elizabeth I, some Turkish Muslim Generals had stayed.</p>
