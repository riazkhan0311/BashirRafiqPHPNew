                        Early in that year, Chaudhry Rehmat Khan fell sick. After some treatment in London, he was repatriated to Pakistan.
                        Nazir Ahmad Hyderabadi arrived in England on 12th April 1966 as Missionary and worked with me until 13th April 1967. During this period, he also served as the Central Finance Secretary. He left on 7th July 1967
                        Laeeq Ahmad Tahir succeeded him as Deputy Imam and worked in that position until 14th December 1970. In this period he rendered valuable service by speaking at several Colleges and Clubs on the subject of Islam. He also worked as a Joint Editor of 'Akhbar e Ahmadiyya'. The Centre in Rabwah appointed him as General Secretary of Majlis Khuddam e Ahmadiyya in the UK.</p>
