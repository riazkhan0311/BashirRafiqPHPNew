                <div class="chapterContent">

                    <p>When I reached London in the early part of 1959 Dr. Abdus Salaam lived in Putney, about a mile and a half from the Fazl Mosque. He was already a Professor of Theoretical Physics at the Imperial College. I had repeated opportunities to meet him. He visited the Mosque very often. Although he occupied a very high position and I was a mere young Missionary he always showed great kindness to me. He would often invite Hadhrat Chaudhry Zafrulla Khan Sahib and some other friends for breakfast at his house. It was always an informal affair and I too would be invited on such occasions. Along with other eulogists, I would also benefit from his discourse. Poetry, literature and current events would normally form subjects of discussion on such occasions. Doctor Sahib would be a guiding light in such assemblies. Later on, when Hadhrat Chaudhry Muhammad Zafrulla Khan took up residence in London and would be invited by Dr. Salaam I would also receive an invitation. </p>
                    <br />

                    <p>Dr. Salaam, apart from his scientific achievements, also showed a great deal of interest in Urdu and Persian poetry. He was a great admirer of 'Hafiz' the famous Persian poet. He had memorised very many of his verses, which he would recite on appropriate occasions. He dearly loved 'Maulana Rum' and had memorised very many of his verses. Doctor Salaam adored Hadhrat Chaudhry Muhammad Zafrulla Khan and always consulted him, even about his personal and private matters. He would act in accordance with the advice tendered.</p>
                    <br />

                    <p>Shortly after my arrival in London his father, Hadhrat Chaudhry Muhammad Hussain, also visited London. Naturally, Doctor Sahib was devoted to his esteemed parent and held them in very high regard. The various outstanding politicians and scientists who came to meet him were always introduced to his father. When it was arranged for Doctor Sahib to meet the Duke of Edinburgh he took his father along with him and introduced him to His Royal Highness.</p>
                    <br />

                    <p>Once he came to the Mission House and said to me that his father, sitting alone in the house got bored. He wondered if it would be in order for him to drop his father at the Mission House in the morning and collect him again in the evening on his way back from the college. He thought that in this way his father would remain involved and would have an opportunity to meet other Ahmadi friends. I said:</p>

                    <div class="quote"> "It would give me great pleasure to have your father at the Mission House. In this way the Jamaat will benefit from him in the field of Tarbiyyat." </div>

                    <p>Therefore, Chaudhry Muhammad Hussain started visiting the Mission House every single day. He would spend a lot of time with me in my office discussing various educational and Tarbiyyati matters. Once he asked me to pray that his son may be awarded the Nobel Prize. I said:</p>

                    <div class="quote"> "You yourself are an extremely pious person. Therefore, compared with me, your prayers will have a ready acceptance."</div>

                    <p>I promised to pray myself and I promised to ask some others to pray for him. A short while later in a dream someone said to me: "Dr. Salaam will surely be awarded the Nobel Prize. He is still very young whereas much older scientists are waiting in the queue. They will be awarded the prize first and then it will be Dr. Salaam's turn." The next day I related this dream to Chaudhry Muhammad Hussain. The following day when Dr. Salaam came to the Mission House to fetch his father he wanted to hear the dream from me directly; I recounted the dream to him. He said:</p>

                    <div class="quote"> "It is strange that a few days ago someone close to the Nobel Prize Committee said the same thing to me."</div>

                    <p>Dr. Salaam was one of the first to reach the Mosque for Friday service and he normally sat in the front row immediately behind the Imam. When I delivered my sermon, Dr. Salaam would make certain notes in his notebook. One day, as a joke I said to him: </p>

                    <div class="quote">"Perhaps you like my sermons so much that every now and then you take notes."</div>

                    <p> The doctor laughed aloud and said:</p>

                    <div class="quote"> "The truth is that every now and then in my mind I receive an electrical impulse which is often brilliant and concerns some scientific issues. I make an immediate note so that in the future they would form the basis of my research. Unless I make a note of them immediately there is a danger of them being lost."</div>

                    <p> This was his custom day and night. Even when he was eating, he would suddenly open his notebook and write something in it. He would then resume the conversation in which he was engaged. He was an arch lover of Ahmadiyyat and had a great sense of honour for it. In 1974 when the Pakistan National Assembly unanimously resolved that Ahmadis were non-Muslims, Dr. Salaam was the Chief Scientific Advisor to the Government of Pakistan holding the rank of a Minister. When he heard the terrible news, he came to the Mission House and showed Hadhrat Chaudhry Zafrulla Khan a letter of resignation that he took out of his pocket. He said:</p>

                    <div class="quote"> "How can I serve such a government that has, exceeding all limits, taken an unjust and shameful decision."</div>

                    <p>Dr. Salaam was deeply shocked when General Zia promulgated the notorious ordinance prohibiting Ahmadis from carrying out Islamic injunctions. After a lapse of some time, I asked him if he had met General Zia ul Haq after the promulgation of the Ordinance. He said he had met him and told me in some detail what had transpired. He said:</p>

                    <div class="quote"> "When I was at Trieste I received repeated calls from General Zia asking me to go and meet him. I made certain excuses but then I had to go on a visit to Pakistan. The General called me and I went to meet him in the Presidents house where many other scientists were already present. The General came out of his office, opened the door of my car, embraced me and took me along to his room where there were some others present. During our conversation, I expressed my disgust on the promulgation of the Ordinance. The General held my hand and invited me to move to another room so that we could talk in private. The Minister responsible for Scientific Affairs also joined us. The General said;
                        <br />

                        "The truth is that some Ulema came to see me in a delegation and told me that the Ahmadis had made certain interpolations in the Holy Quran which hurt me. They told me that because of these interpolations they should be excluded from the fold of Islam."</div>

                    <p>I said to the General; </p>

                    <div class="quote">"A promise to protect the Holy Quran is present in the Quran itself where the Almighty, addressing the Holy Prophet said that He Himself would protect the Quran. Because of this Divine Promise how can any Ahmadi possibly make any interpolations in the Holy Quran?"</div>

                    <p> The General got up, walked to a bookshelf and picked up 'Tafseer e Sagheer'. He said;</p>

                    <div class="quote">"This contains the Urdu translation of the Holy Quran by Mirza Mahmmod Ahmad. In it the Ulema have marked those verses in which Jamaat Ahmadiyya has been responsible for interpolation. I will show you some."</div>

                    <p> He opened the 'Tafseere Sagheer', put his finger on a spot, which was already marked and said;</p>

                    <div class="quote">"Here you have made an interpolation."</div>

                    <p> I said;</p>

                    <div class="quote">"The verse has been produced in its entirety, where is the interpolation?"</div>

                    <p> The General responded by saying:</p>

                    <div class="quote">"Look here, in this particular verse you are guilty of interpolation as you have translated the verse containing the words 'Khatam un Nabiyyeen' as 'Seal of Prophets'. We cannot possibly bear this."</div>

                    <p>I said;</p>

                    <div class="quote">"The word Khatam used in the Quran is neither a Punjabi word nor an English word and in the Arabic language it means a 'seal'. I do not wish to be involved in an argument. Have you available to you a translation by some other scholars?"</div>

                    <p> The General rose and brought a copy of the Holy Quran with translation by Allama Asad, which had been published from Mecca. I opened the Quran and found that the word 'Khatam un Nabiyyeen' had been translated in it as 'Seal of Prophets'. The General seemed flabbergasted. I said;</p>

                    <div class="quote"> "General! Allama Asad was not an Ahmadi and his translation has been published by the Saudi Government. Would you then find the Saudi Government guilty of interpolation?"</div>

                    <p> The General responded by saying;</p>

                    <div class="quote">"I am only an illiterate General and I accepted as the truth whatever the scholars told me."</div>

                    <p> I said;</p>

                    <div class="quote">"You are not just a General but President of a country and as such it is your responsibility to protect the rights of all sections of the population. Here in Islamabad itself the Jamaat Ahmadiyya has stationed a Missionary. Many members of the Jamaat also live here. Was it not your responsibility to send for some Ahmadi scholars and satisfy yourself? Should you not have heard both sides before taking a decision?"</div>

                    <p> On this, the General read 'Kalima Shahadah' aloud and invited me to do the same. In a loud voice I read the 'Kalima Shahadah."</p>

                    <p> Then the General said;</p>

                    <div class="quote">"Salaam, I swear by God that I regard you a better Muslim than myself but what could I do when I was pushed into a corner by the Ulema?"</div>

                    <p>Then he switched to another subject.</p>

                    <p>Dr. Salaam was wholly and utterly devoted to Pakistan and loved his country. When I obtained my British passport, I suggested to him at the breakfast table that he should also apply for British nationality and get a British passport, as on a British passport he would find travelling in foreign countries a lot easier. I offered to bring the application form for a British passport across for him. He remained quiet for a while and then said:</p>

                    <div class="quote"> "I will never abandon my Pakistan nationality. I am hopeful that before long I will be awarded the Nobel Prize and on my account I would hate to see a country other than Pakistan to be recipient of that honour. I am a Pakistani and I will always remain a Pakistani even though I may have problems in my travels." </div>

                    <p>Therefore, throughout his life, he remained a Pakistani and he died a Pakistani.
                        He was utterly devoted to his parents. When his father passed away, he was grief stricken and confined himself to his house. A few days later I heard from his wife that he had taken the event to heart and could not devote his mind to any other subject. She said: </p>

                    <div class="quote">"Kindly ask Hadhrat Chaudhry Sahib to visit and console him." </div>

                    <p>When I mentioned this to Chaudhry Sahib, he visited Dr. Salaam who was in a bad way due to his grief. Hadhrat Chaudhry Sahib made an effort to console him and said: "Grief beyond a certain degree is liable to become shirk." Quoting himself as an example he said that although he was an arch lover of his own mother, on her demise he patiently became reconciled and accepted the Will of Allah. After talking to him for a while, he embraced Dr. Salaam and held him in his arms for a long time. Dr. Salaam broke down and wept bitterly. That is how he lightened the burden of his grief. Both his parents were 'Olia of Allah' and were recipient of visions. They were prayerful. They dearly loved God's creation and came to the assistance of the poor. </p>
                    <br />

                    <p>Mrs Abd us Salaam served as Sadar (President) of Lajna Imaullah UK for very many years. Throughout the period when I was Imam and Missionary in charge in the UK, I received complete co-operation from her. She always remained engaged in service to the community. She also remained engaged in taking care of and showing hospitality towards the guest of her husband.</p>
                    <br />

                    <p>Very recently, by the sheer mercy and grace of Allah, another relationship has been established between my family and the family of Dr. Salaam. Dr. Salaam's grandchild (a son of his daughter) Faiz ur Rahman, who is a son of Dr. Hameed ur Rahman and Azeeza Salaam, is married to my granddaughter Madeeha Henna Khan, a daughter of Abd ul Waheed Khan and Amat un Naseer (Neeno).</p>

