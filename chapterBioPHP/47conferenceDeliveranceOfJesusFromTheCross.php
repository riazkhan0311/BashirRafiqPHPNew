                    <p>In May 1977, I read in the papers that throughout the year 1978 various Christian organizations would research the 'Shroud' and in this regard very many seminars would be held.</p>
                    <br />

                    <p>The Holy Shroud is a sacred garment in which Jesus Christ was wrapped after he had been taken off the cross. This sacred shroud is safely lodged in a church in Turin in Italy. It seems significant that, when it was photographed for the first time in the middle of the 19th century, someone noticed that the hands and feet had been injured and bloodstains from his ribs were visible. According to the belief of the Ahmadis, this is an image of Jesus Christ. The dripping, trickling or oozing of the blood on the cloth is clear proof that when Jesus Christ was wrapped in the Shroud, his heart was still pumping and he was alive. Obviously, blood does not drip, trickle or ooze from a dead body. This is a very wide subject and very many books have been written on it.</p>
                    <br />

                    <p>On perusal of the reports of the Shroud in the newspapers and as the year 1978 was chosen for research I wrote to Huzoor and suggested that perhaps advantage could be taken of this happening and may the British Jamaat be permitted to hold an International Conference on the subject of : 'The Deliverance of Jesus from the Cross'.</p>
                    <br />

                    <p>I requested that Huzoor should also participate in the conference. Huzoor liked the proposal and granted permission for the Conference to be held and he promised to personally participate in it. He also sanctioned the necessary expenditure to be incurred in this connection. For further consultations, I was asked to visit Rabwah for a week. Accordingly, I reported at Rabwah in 1977 and in the presence of Wakeel ul Tabshir Huzoor gave me detailed instructions. He approved the proposed program for the conference.</p>
                    <br />

                    <p>This anticipated conference was held on the 2nd, 3rd and 4th June 1978 in the Commonwealth Centre in Kensington in London. During the first two days, some world famous scholars read their papers on the subject of "Deliverance of Jesus from the Cross".</p>
                    <br />

                    <p>By the Grace of Allah, the conference was extraordinarily successful. More than 1,500 persons from many religions, and many countries attended. Every day some very distinguished scholars also graced the occasion with their presence.</p>
                    <br />

                    <p>Before the conference 'The Sunday Telegraph' asked me to provide them with the necessary material so that they may produce a special issue. In this connection, I had already undertaken a tour of Kashmir, which I have mentioned separately. On the last day of the conference i.e. a Sunday, 'The Sunday Telegraph' published a special edition, which included reports of interviews with Khalifa tul Masih III, and an interview with Hadhrat Chaudhry Muhammad Zafrulla Khan. They published a photograph of the mausoleum of Jesus Christ in Srinagar. I have already provided the necessary details elsewhere. The conference produced an upheaval in the Christian Church. We had, through papers, magazines, TV and radio prepared a plan to cover the conference. By the Grace of Allah, it was most successful. The British TV and the radio broadcast several interviews with me. 'The Times', 'The Telegraph', 'The Daily Guardian' etc. published the proceedings of my press conferences. Our message being: Jesus Christ did not die on the cross but was miraculously saved from the cross and at the age of 120 years died a natural death in Srinagar, Kashmir where he is buried.</p>
                    <br />

                    <p>A few days before the commencement of the conference I began to receive threatening calls from some Christian circles. It was said that my children would be abducted and the assets of the Jamaat might be damaged. I told the Police about these calls and as a result, for a few days before and for a few days after the conference, my children went to and returned from school in Police protection. On the inaugural day of the conference, I received an anonymous call telling me that a bomb had been placed in the Commonwealth Conference Hall. The Police surrounded the Commonwealth Conference Hall and mounted a thorough search. The conference only started after clearance from the Police.</p>
                    <br />

                    <p>One the day prior to the commencement of the conference an announcement was made on the Radio that the British Council of Churches had made certain proposals to Bashir Ahmad Rafiq, Imam of the London Mosque. I advised Huzoor that I had not received any proposals. I was instructed to telephone the British Council of Churches and tell them that no letter had been received. The Secretary of the Council apologized and explained that an announcement on the Radio had been made prematurely and that in the near future I would receive the letter. Huzoor told me that in his final address he would himself respond to the letter. Therefore, at the end of the conference Huzoor did reply to the letter.</p>
                    <br />

                    <p>The total proceedings of the conference are a part of the history of Ahmadiyyat and will, Inshallah, be published in its entirety. It is a pity that although Huzoor had accepted the invitation from the Church for a debate and although the Church had formed a committee for a debate they paid no further attention to this matter and corroborates the suspicion that it was never their intention to hold a debate. This step was taken only to divert attention from the conference where they had to cut a sorry figure.</p>
                    <br />

                    <p>I have compiled a book covering the proceedings of the conference called 'Deliverance of Jesus from the Cross' which has been published and has been well received.</p>
                    <br />

                    <p>During the preparation for the conference I addressed a Press Conference in Birmingham, a report of which was published in many British papers. A glimpse also appeared on the TV the same evening. The following day the TV recorded a dialogue between the Bishop of Birmingham and me. The Bishop was unable to rebut the arguments that I presented in support of my belief that Jesus Christ did not die on the cross. Members of the Jamaat highly appreciated this program.</p>
                    <br />

                    <p>At the end of the conference, Mr Tom Cox MP held a reception to honor Hadhrat Khalifa tul Masih III in the House of Commons cafeteria. Apart from members of the Jamaat, a large number of members of the British Parliament also attended and met Huzoor. Naturally, some very interesting subjects came under discussion.</p>
                    <br />

                    <p>During the reception the Parliament was in session and I escorted Huzoor to the public gallery. After a few minutes an office bearer of the Parliament told me that the Speaker of the House was curious, as to whom the distinguished visitor was in the public gallery. The office bearer told me that the Speaker had been deeply impressed by Huzoor and enquired of certain details concerning him. On our way back from the public gallery we met Mr Edward Heath, a former Prime Minister of Great Britain. Years earlier both Mr Heath and Huzoor had been classmates at Oxford. Huzoor asked Mr Heath if he recognized him. Without the slightest hesitation, Mr Heath said:</p>


                    <div class="quote">"Mirza Nasir Ahmad"</div>


                    <p>Huzoor was also surprised that after a lapse of so many years Mr Heath had recognized him. They talked for a while. On being asked Huzoor explained to the former Prime Minister certain details regarding the institution of Khilafat. </p>
                    <br />

                    <p>A somewhat similar incident had occurred a little earlier. Huzoor had studied at Balliol College, Oxford. When along with his wife and family he visited the College I accompanied him. The Begum Sahiba was keen to see the college where her husband had been and Huzoor wanted to refresh his memory of the earlier days. On our arrival, an old man on guard duty demanded to see our passes. Huzoor asked him:</p>


                    <div class="quote">"Don't you recognize me?"</div>


                    <p>We were greatly surprised when, after carefully looking at Huzoor's face, he declared:</p>



                    <div class="quote">"Nasir Ahmad."</div>


                    <p>Huzoor was extremely pleased and shook his hand. The guard reminded him of certain events in the days gone by. In all probability, the guard had seen Huzoor after 35 years and in the meantime, quite naturally, Huzoor's demeanor had changed incredibly. One must pay a tribute to the memory of the guard who recognized him and then showed esteem for him.</p>
                    <br />

                    <p>At the end of the conference, the British Jamaat arranged a big picnic for Huzoor in the Birmingham area. The picnic was held in the Stately Home of a well-known peer. On that day, the local papers published details of the event with photographs in their special issue. By the Grace of Allah, this conference was extremely successful.</p>


