                        <h2>Arrival at Liverpool.
                            <br>
                            <small>1960</small>
                        </h2>
                        <br>
                        <p>Early in the morning on 18th February 1959, our ship dropped anchor at Liverpool and thus our voyage ended. However, because of very thick fog it was impossible to disembark immediately. Once the fog had lifted, we went ashore and boarded the train for London. We reached Euston station by 6 o’clock. It was strange that at the point of disembarkation from the ship no one checked our Passports. An official sat on a chair and from a distance the passengers showed him their Passports and proceeded. No entry was made on the Passport nor was it stamped. </p>
                        <br>
                        <p>Those days were strange; there was no need for an Entry Permit and nor was there a problem about Visas. Having retrieved our baggage we heard an announcement that the London Boat train was about to depart and that the passengers should board. That was the first time that we had travelled on a British train and we were deeply impressed with the cleanliness of it. There was no comparison at all with trains in the Indian Sub Continent. Here they were neat and clean and they ran on time. May our country learn a lesson from the British? </p>
