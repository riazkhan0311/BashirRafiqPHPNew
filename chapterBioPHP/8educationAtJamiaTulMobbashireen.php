                    <p> After I had graduated in 1953, I went to see Hadhrat Khalifa tul Masih II. He directed me to be enrolled in the Jamia for the 'Shahid' Degree course. He said that it was his desire to send me into the field of Tableegh.</p>
                    <br />
                    <p>In accordance with Huzoor's wishes, I went to see Hadhrat Maulana Abul Ata who was then Principal of the Jamia. To begin with, he questioned me about my proficiency in Arabic. I admitted that I only had a slight knowledge of Arabic. I said that in my school and in the college I had studied Persian and therefore my knowledge of the Arabic language was limited to religious subjects. The Maulana said:</p>
                    <br />

                    <div class="quote">"Normally students who enroll in the Jamia hold Honors Degrees in Arabic. How will you manage alongside such classmates?"</div>
                    <br />

                    <p>I said that I had come to him in obedience to Khalifa tul Masih II's instructions; otherwise I was fully conscious that I was not worthy of studying Arabic subjects. The Maulana said:</p>
                    <br />

                    <div class="quote">"Never mind, for your sake I will start a special class in which you will be the only student. In a period of two years you will learn sufficient Arabic to pull along with other students who are already graduates in Arabic."</div>
                    <br />

                    <p>The class was established and I was the only student in it. The teachers chosen for this special class were some of the best scholars of the Jamaat such as Hadhrat Maulana Abdul Ata, Hadhrat Malik Saif ur Rahman, Hadhrat Maulana Abul Munir Noor ul Haq, Hadhrat Maulana Muhammad Ahmad Jaleel, Hadhrat Maulana Ghulam Bari Saif and Hadhrat Maulana Saqib. Under the directions of these scholars, I studied Commentary of the Holy Quran, Traditions of the Holy Prophet, Jurisprudence, Christian and Islamic History. At the beginning of the third year, I was judged good enough to join the other students. The Jamia course was spread over a period of five years.</p>
                    <br />
                    <p>In the very beginning, the surroundings and the atmosphere of the Jamia were very challenging for me. It was very different from the surroundings and atmosphere prevailing in the T I College. However, gradually, I made the necessary adjustments. In the Jamia, all students were trained to write articles and to speak in public. Even when I was in college, I was having a fondness for writing articles. In the Jamia, I was enabled to pay special attention to this exercise. During that period, some of my articles appeared in the 'Al Fazl' and 'Al Furqan'. Hadhrat Khalifa tul Masih II liked my efforts. He particularly liked an article published in instalments in the 'Al Fazl' headed as 'Jamaat e Islami in Pakistan'. I was much encouraged. During my college days, I frequently secured first and second positions in declamation contests. Therefore, even in the Jamia I gained distinction in the speeches I delivered. Our class in the Jamia was honored by a few visits by Hadhrat Khalifa tul Masih II. He would lay emphasis on gaining proficiency in various subjects. He directed that each student should have his own separate personal library. He asked the students to acquire the habit of purchasing books. He mentioned that half of the pocket money, given to him by his father the Promised Messiah (pbuh), was used up for purchasing newspapers and periodicals. He said that in this way his own library was established which he subsequently passed on to the Jamaat. Later, those books were transferred to the Khilafat Library. In order to encourage students to maintain their individual libraries he directed the Jamia to advance Rs.100 to each student.</p>
                    <br />

                    <p>The advance was to be repaid in easy installments. In this way, Huzoor hoped that the students would purchase books and establish their own libraries. He also directed that all books published by the Jamaat should be made available to the students at half the normal price. Consequently, I also organised my own personal library. I purchased all the books of the Movement that were then available and in addition, I acquired books on the subjects of Islamic History, Commentary of the Holy Quran and Traditions of the Holy Prophet (saw). I still habitually follow the directions issued by Huzoor since I was a student in Jamia. In the interim period, due to frequent transfers of residence, some of my books got lost. However, my love for books continues unabated. Reading and study of books continues to be my pastime.</p>
                    <br />
                    <p>My favorite books are on the subjects of Islamic history, biography and travel. At one time, I read a lot of English and Urdu fiction and at one stage; someone discouraged me from reading fiction. I mentioned this to Hadhrat Khalifa tul Masih II and he said that he also read fiction but only English fiction. He said that in Urdu fiction, there is often a great deal of exaggeration and falsehood and therefore he did not read Urdu fiction any more. He told me that if I had to read fiction I should confine myself to English fiction. He suggested that as a result my English might improve. During my college days some how I lost interest in fiction and now I do not go near it.
                        I have always been interested in Urdu literature and poetry and this interest continues unabated. My library contains the works of very many poets.
                        In 1957, after I graduated as a 'Shahid' from the Jamia, in accordance with the instructions of Hadhrat Khalifa tul Masih II, I reported to the Wakalat Tabsheer.</p>


