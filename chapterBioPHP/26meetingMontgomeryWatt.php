                    <p>Even when I was a student, I had heard the name of Mr Montgomery Watt and I had been impressed by the fact that he had written a number of books on Islam. Although I did not get an opportunity to read his books when I was at college, on arrival in England, however, I had the urge to read his books, particularly 'Muhammad at Mecca' and 'Muhammad at Medina'. I was also keen to meet him. He was then Professor of Islamic Studies at the Edinburgh University. I wrote to him and expressed a desire to meet him. I was surprised to receive his reply telling me that he would soon be visiting London. He also told me when he would be free for a meeting. A few days later, I invited him for lunch at a restaurant. That was our first meeting. I asked him how he had become interested in Islam. He said that when in 1937 his mother died suddenly, as a mere student, inevitably he had to face some financial problems. It therefore became necessary for him to advertise and look for a paying guest. A student of Veterinary Medicine moved in with him.</p>
                    <br />

                    <P>The young guest was an Ahmadi and they often exchanged views over breakfast. The young student displayed a great sense of honor for Islam and it was he who introduced him to Islam. Then he became interested in a detailed study of religions. After an in depth study, to gain further knowledge of Islam, he visited some Arab countries. He completed his PhD at Edinburgh on "<em><strong>Freewill and predestination in early Islam"</strong></em><strong></strong>.</p>
                    <br />

                    <p>Between 1944 and 1946 he worked in Palestine under the Bishop of Jerusalem. In 1947 he became head of the department of Arabic and Islamic Studies at Edinburgh University from where he retired in 1979. He received the title of Professor.</p>
                    <br />

                    <p>Only then, he said, he felt that he was competent to write books on Islamic subjects. This meeting was not only very interesting but also very informative. I presented him with some Ahmadi publications, some of which, he said, he had already read.</p>
                    <br />

                    <p>A little later, the Ahmadiyya UK Mission organized a meeting on 'Seerat un Nabi'. I invited Mr Montgomery Watt to participate and speak on the life of the Holy Prophet (saw). Accepting the invitation he visited London and stayed in a nearby Hotel. However, I insisted that, on the two days of the proceedings, he should have both meals with me at my residence. By then Hadhrat Chaudhry Zafrulla Khan had moved into the upper floor flat in the Mission House. He joined us for both meals and for me those lunches and dinners became treasures of knowledge and devotion.</p>
                    <br />


                    <p>Hadhrat Chaudhry Sahib would talk about a variety of incidents in his own life, such as meetings with the Promised Messiah (pbuh) and his successors. He also touched upon some political subjects. I would often invite other guests to join us for meals and they too profited from his company. {This practice continued for many years and I benefited enormously from this spring of benevolence.}
                        Once when Professor Montgomery Watt was invited for lunch at my residence, he met Chaudhry Sahib and this was a most fascinating encounter. Two world famous dignitaries sat at the same table and talked about Islam. Chaudhry Sahib said to Mr Montgomery Watt:</p>

                    <div class="quote">"I was deeply disappointed on reading your book 'Muhammad at Mecca'.
                        In it you had leveled erroneous and unsavory allegations against the Holy Prophet of Islam (saw). After reading this book, I had made up my mind never to read another book written by you. However, a little later, a friend presented to me your book 'Muhammad at Medina'. I told my friend that I was determined never to read another book written by Mr Watt, as in his first book, mention of the Holy Prophet of Islam (saw), was not really based on truth. I felt that you had written that book wearing prejudicial glasses. My friend told me that in the new book you would find that the attitude of the author had altered radically. He urged me to read it. When I read it I was greatly surprised and pleased as I found that your attitude had undergone a complete transformation. In the new book you have presented the various events in the correct light." </div>

                    <p>Mr Watt replied:</p>

                    <div class="quote">"Sir, you have correctly assessed the situation. When I wrote my first book 'Muhammad at Mecca' my knowledge of Islam was strictly limited. After further studies I became familiar with the exalted and lofty station of the Holy Prophet of Islam (saw). By writing my second book 'Muhammad at Medina', I have made a sincere attempt to compensate for the earlier errors. I have now attempted to present the various events in the correct light."</div>

                    <p>Although he was an author of more then thirty books, I was greatly impressed by his humility. He had already written many books on Islam that had gained great fame and acceptance. He had established friendly relationships with a number of Heads of State, Kings and high officials in the Arab World. Even in the remotest corner of his mind, I found no trace of pride or arrogance. He always met me with warmth and listened attentively to whatever I had to say. Even obliquely, he never hinted that he was a renowned scholar. He displayed the same attitude when he met Chaudhry Sahib. He sat with him respectfully as if Chaudhry Sahib was his tutor.</p>


